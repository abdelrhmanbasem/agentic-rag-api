class BookingSubagent:
    """
    Booking executor for the LangGraph architecture.

    Responsibilities:
    - maintain booking state
    - extract deterministic fields
    - resolve canonical branch from stored branch state or configured branch aliases
    - resolve normalized appointment dates from natural date phrases
    - resolve slot selection
    - collect customer details without asking repeatedly for already captured fields
    - call list_available_slots
    - call create_booking only after explicit confirmation and required details
    - protect against duplicate booking
    - filter slots by service_needed when known

    Not responsible for final customer-facing wording.
    Final wording is generated by graph.response_node and validated by quality_guard.
    """

    name = "booking"

    def get_config(self, assistant_config: Dict[str, Any]) -> Dict[str, Any]:
        return get_subagent_config(assistant_config, self.name)

    def run(self, context: SubagentContext) -> SubagentResult:
        config = self.get_config(context.assistant_config)

        if not config.get("enabled", False):
            return SubagentResult(handled=False)

        variables = dict(context.variables or {})
        normalization = context.assistant_config.get("normalization", {})
        variables = self.ensure_valid_customer_profile(
            variables=variables,
            message=context.user_message,
            normalization=normalization
        )
        observations: List[Dict[str, Any]] = []
        tool_calls_used = 0

        stage_path = config.get("stage_path", "booking.stage")
        stage = deep_get(variables, stage_path, "")

        variables = self.ensure_selected_branch_from_context(
            variables=variables,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        # Critical slot-selection guard:
        # In slot_selection stage, a message like "الساعة 12" or "معاد 12"
        # must be resolved against the existing available_slots before any
        # date extraction runs. Otherwise it can be misclassified as date_text.
        if stage == "slot_selection":
            selected_slot_before_date = self.resolve_slot_selection(
                context=context,
                config=config,
                variables=variables
            )

            if selected_slot_before_date:
                return self.handle_slot_selected(
                    context=context,
                    config=config,
                    variables=variables,
                    selected_slot=selected_slot_before_date
                )

        if self.is_booking_completed(variables):
            if self.is_polite_closing(context.user_message, config, normalization):
                answer = render_template(
                    config.get("templates", {}).get(
                        "booking_completed_closing",
                        "BOOKING_COMPLETED_CLOSING"
                    ),
                    {
                        "variables": variables,
                        "message": context.user_message
                    }
                )

                return SubagentResult(
                    handled=True,
                    action="reply",
                    answer=answer,
                    variable_updates=variables,
                    selected_subagent=self.name,
                    observations=observations,
                    tool_calls_used=tool_calls_used,
                    notes="booking already confirmed and user sent closing message"
                )

            if self.looks_like_duplicate_confirmation(context.user_message, config, normalization):
                answer = render_template(
                    config.get("templates", {}).get(
                        "booking_already_confirmed",
                        "BOOKING_ALREADY_CONFIRMED"
                    ),
                    {
                        "variables": variables,
                        "message": context.user_message
                    }
                )

                return SubagentResult(
                    handled=True,
                    action="reply",
                    answer=answer,
                    variable_updates=variables,
                    selected_subagent=self.name,
                    observations=observations,
                    tool_calls_used=tool_calls_used,
                    notes="booking already confirmed; duplicate confirmation blocked"
                )

        date_text = self.extract_date_text(
            message=context.user_message,
            config=config,
            normalization=normalization
        )

        if date_text and self.should_extract_date(context, config, stage):
            date_updates = {
                config.get("date_text_path", "date_text"): date_text
            }

            resolved_date = self.resolve_date_text(
                date_text=date_text,
                message=context.user_message,
                normalization=normalization
            )

            if resolved_date:
                date_updates[config.get("appointment_date_path", "appointment_date")] = resolved_date
                date_updates["date"] = resolved_date

            variables = apply_variable_patch(
                variables,
                date_updates,
                []
            )

        variables = self.ensure_resolved_date_from_existing_state(
            variables=variables,
            config=config,
            normalization=normalization,
            message=context.user_message
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        extraction_active_stages = config.get("extraction_active_stages", [
            config.get("stages", {}).get("awaiting_customer_details", "awaiting_customer_details")
        ])

        should_extract_customer_details = stage in extraction_active_stages
        extracted: Dict[str, Any] = {}

        if should_extract_customer_details:
            extracted = self.extract_customer_details(
                message=context.user_message,
                config=config,
                variables=variables,
                normalization=normalization
            )

            if extracted:
                variables = apply_variable_patch(variables, extracted, [])

            variables = self.ensure_valid_customer_profile(
                variables=variables,
                message=context.user_message,
                normalization=normalization
            )

        stage = deep_get(variables, stage_path, stage)

        if stage == config.get("stages", {}).get("awaiting_confirmation", "awaiting_confirmation"):
            return self.handle_awaiting_confirmation(
                context=context,
                config=config,
                variables=variables,
                observations=observations,
                tool_calls_used=tool_calls_used
            )

        if stage == config.get("stages", {}).get("awaiting_customer_details", "awaiting_customer_details"):
            return self.handle_awaiting_customer_details(
                context=context,
                config=config,
                variables=variables,
                observations=observations,
                tool_calls_used=tool_calls_used
            )

        selected_slot = self.resolve_slot_selection(
            context=context,
            config=config,
            variables=variables
        )

        if selected_slot:
            return self.handle_slot_selected(
                context=context,
                config=config,
                variables=variables,
                selected_slot=selected_slot
            )

        if stage == "slot_selection":
            if self.is_new_availability_request(
                context=context,
                config=config,
                variables=variables
            ):
                return self.handle_booking_request(
                    context=context,
                    config=config,
                    variables=variables,
                    observations=observations,
                    tool_calls_used=tool_calls_used
                )

            answer = render_template(
                config.get("templates", {}).get("choose_slot_again", "BOOKING_SLOT_SELECTION_UNCLEAR"),
                {
                    "variables": variables,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="ask_user",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes="slot_selection unresolved"
            )

        if self.is_booking_or_availability_request(
            context=context,
            config=config,
            variables=variables
        ):
            return self.handle_booking_request(
                context=context,
                config=config,
                variables=variables,
                observations=observations,
                tool_calls_used=tool_calls_used
            )

        if extracted:
            return SubagentResult(
                handled=False,
                variable_updates=variables,
                selected_subagent=self.name,
                notes="customer detail extraction only"
            )

        return SubagentResult(handled=False)

    def handle_awaiting_confirmation(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        observations: List[Dict[str, Any]],
        tool_calls_used: int
    ) -> SubagentResult:
        normalization = context.assistant_config.get("normalization", {})
        confirmation_phrases = config.get("confirmation_phrases", [])
        rejection_phrases = config.get("rejection_phrases", [])

        variables = self.ensure_selected_branch_from_context(
            variables=variables,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        if self.is_booking_completed(variables):
            answer = render_template(
                config.get("templates", {}).get("booking_already_confirmed", "BOOKING_ALREADY_CONFIRMED"),
                {
                    "variables": variables,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="reply",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes="booking already confirmed; create_booking blocked"
            )

        if matches_any(context.user_message, rejection_phrases, normalization):
            updates = config.get("on_reject_updates", {})
            clear = config.get("on_reject_clear", [])

            patched = apply_variable_patch(
                variables,
                updates if isinstance(updates, dict) else {},
                clear if isinstance(clear, list) else []
            )

            answer = render_template(
                config.get("templates", {}).get("slot_rejected", "BOOKING_SLOT_REJECTED"),
                {
                    "variables": patched,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="ask_user",
                answer=answer,
                variable_updates=patched,
                clear_variables=[],
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes="pending booking rejected"
            )

        # In confirmation stage, do not allow generic extraction to treat
        # confirmation-only replies like "اه اكده تمام" as a customer name.
        details: Dict[str, Any] = {}

        if self.has_customer_detail_signal(context.user_message, normalization):
            details = self.extract_customer_details(
                message=context.user_message,
                config=config,
                variables=variables,
                normalization=normalization
            )

            if details:
                variables = apply_variable_patch(variables, details, [])

        variables = self.ensure_valid_customer_profile(
            variables=variables,
            message=context.user_message,
            normalization=normalization
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        if not matches_any(context.user_message, confirmation_phrases, normalization):
            answer = render_template(
                config.get("templates", {}).get(
                    "repeat_confirmation",
                    "BOOKING_AWAITING_EXPLICIT_CONFIRMATION"
                ),
                {
                    "variables": variables,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="ask_user",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes="awaiting explicit confirmation"
            )

        variables = apply_variable_patch(
            variables,
            config.get("on_confirm_updates", {}),
            []
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        missing = self.get_missing_required_create_fields(config, variables)

        if missing:
            answer = self.render_missing_question(config, variables, missing, context.user_message)

            variables = apply_variable_patch(
                variables,
                config.get("on_missing_details_updates", {}),
                []
            )

            variables = self.ensure_pending_booking_context(
                variables=variables,
                config=config,
                assistant_config=context.assistant_config,
                message=context.user_message,
                normalization=normalization
            )

            return SubagentResult(
                handled=True,
                action="ask_user",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes=f"confirmed slot but missing customer details: {missing}"
            )

        return self.call_create_booking(
            context=context,
            config=config,
            variables=variables,
            observations=observations,
            tool_calls_used=tool_calls_used
        )


