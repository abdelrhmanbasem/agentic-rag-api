    def choose_date_candidate(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        existing_date: str = "",
        prefer_different: bool = False
    ) -> Dict[str, Any]:
        candidates = self.resolve_date_candidates(
            message=message,
            config=config,
            normalization=normalization
        )

        if not candidates:
            return {}

        existing = str(existing_date or "").strip()

        if prefer_different and existing:
            for candidate in candidates:
                resolved = str(candidate.get("resolved_date") or "").strip()
                if resolved and resolved != existing:
                    return candidate

        return candidates[0]

    def resolve_date_candidates(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> List[Dict[str, Any]]:
        candidates = self.extract_date_candidates(
            message=message,
            config=config,
            normalization=normalization
        )

        resolved_candidates: List[Dict[str, Any]] = []
        seen = set()

        for candidate in candidates:
            date_text = str(candidate.get("date_text") or "").strip()

            if not date_text:
                continue

            resolved = self.resolve_date_text(
                date_text=date_text,
                message=message,
                normalization=normalization,
                config=config
            )

            key = (
                normalize_text(date_text, normalization),
                str(resolved or "")
            )

            if key in seen:
                continue

            seen.add(key)

            resolved_candidates.append({
                **candidate,
                "resolved_date": resolved
            })

        return resolved_candidates

    def extract_date_candidates(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> List[Dict[str, Any]]:
        raw_message = normalize_digits(str(message or ""), normalization.get("digit_map", {})).strip()

        if not raw_message:
            return []

        normalized = normalize_text(raw_message, normalization)

        if self.looks_like_time_only_slot_selection_text(
            message=raw_message,
            config=config,
            normalization=normalization
        ):
            return []

        candidates: List[Dict[str, Any]] = []

        def add_candidate(value: str, source: str, start: int, end: int):
            cleaned = self.clean_date_text(str(value or "").strip(), normalization, config)

            if not cleaned:
                return

            normalized_cleaned = normalize_text(cleaned, normalization)

            if not normalized_cleaned:
                return

            candidates.append({
                "date_text": cleaned,
                "source": source,
                "start": max(int(start or 0), 0),
                "end": max(int(end or 0), 0),
                "score": len(normalized_cleaned)
            })

        direct_terms = config.get("date_direct_terms", [])
        if isinstance(direct_terms, list):
            for term in direct_terms:
                term_text = str(term or "").strip()
                if not term_text:
                    continue

                normalized_term = normalize_text(term_text, normalization)
                if not normalized_term:
                    continue

                search_from = 0
                while True:
                    idx = normalized.find(normalized_term, search_from)
                    if idx < 0:
                        break

                    add_candidate(term_text, "direct_term", idx, idx + len(normalized_term))
                    search_from = idx + max(len(normalized_term), 1)

        explicit_patterns = config.get("explicit_date_patterns", [])
        if isinstance(explicit_patterns, list):
            for item in explicit_patterns:
                pattern = ""
                group = 1

                if isinstance(item, dict):
                    pattern = str(item.get("regex", "") or "")
                    try:
                        group = int(item.get("group", 1))
                    except Exception:
                        group = 1
                else:
                    pattern = str(item or "")

                if not pattern:
                    continue

                try:
                    matches = list(re.finditer(pattern, raw_message, flags=re.IGNORECASE))
                except re.error:
                    continue

                for match in matches:
                    try:
                        value = match.group(group).strip()
                        span = match.span(group)
                    except Exception:
                        value = match.group(0).strip()
                        span = match.span(0)

                    add_candidate(value, "explicit_pattern", span[0], span[1])

        for item in config.get("date_extraction_patterns", []):
            if not isinstance(item, dict):
                continue

            regex = item.get("regex", "")
            try:
                group = int(item.get("group", 1))
            except Exception:
                group = 1

            if not regex:
                continue

            try:
                matches = list(re.finditer(regex, raw_message, flags=re.IGNORECASE))
            except re.error:
                continue

            for match in matches:
                try:
                    value = match.group(group).strip()
                    span = match.span(group)
                except Exception:
                    value = match.group(0).strip()
                    span = match.span(0)

                add_candidate(value, "date_extraction_pattern", span[0], span[1])

        if not candidates:
            return []

        candidates.sort(key=lambda item: (item.get("start", 0), -item.get("score", 0)))

        output: List[Dict[str, Any]] = []
        seen_terms = set()

        for candidate in candidates:
            key = normalize_text(str(candidate.get("date_text") or ""), normalization)
            if key in seen_terms:
                continue

            seen_terms.add(key)
            output.append(candidate)

        return output


    def extract_date_text(self, message: str, config: Dict[str, Any], normalization: Dict[str, Any]) -> str:
        candidate = self.choose_date_candidate(
            message=message,
            config=config,
            normalization=normalization
        )

        return str(candidate.get("date_text") or "").strip() if candidate else ""


    def looks_like_time_only_slot_selection_text(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> bool:
        raw_message = str(message or "").strip()

        if not raw_message:
            return False

        normalized = normalize_text(raw_message, normalization)

        date_terms = config.get("date_direct_terms", [])

        if isinstance(date_terms, list):
            ambiguous_booking_words = set()
            time_only_config = config.get("time_only_slot_selection", {})
            if isinstance(time_only_config, dict):
                raw_ambiguous = time_only_config.get("ambiguous_date_terms", [])
                if isinstance(raw_ambiguous, list):
                    ambiguous_booking_words = {
                        normalize_text(str(item or ""), normalization)
                        for item in raw_ambiguous
                        if str(item or "").strip()
                    }

            for term in date_terms:
                term_text = str(term or "").strip()

                if not term_text:
                    continue

                normalized_term = normalize_text(term_text, normalization)

                if normalized_term in ambiguous_booking_words:
                    continue

                if normalized_term and normalized_term in normalized:
                    return False

        if self.extract_explicit_date_phrase(raw_message, config):
            return False

        requested_time = self.extract_time(raw_message, config, normalization)

        if not requested_time:
            return False

        time_only_config = config.get("time_only_slot_selection", {})
        if not isinstance(time_only_config, dict):
            time_only_config = {}

        markers = time_only_config.get("time_markers", [])

        if not isinstance(markers, list):
            markers = []

        for marker in markers:
            normalized_marker = normalize_text(str(marker or ""), normalization)
            if normalized_marker and normalized_marker in normalized:
                return True

        allow_bare_numeric = bool(time_only_config.get("allow_bare_numeric", True))

        if not allow_bare_numeric:
            return False

        bare_numeric_regex = time_only_config.get(
            "bare_numeric_regex",
            r"(^|\D)(\d{1,2})(?::\d{2})?(\D|$)"
        )

        try:
            return bool(re.search(str(bare_numeric_regex), normalized))
        except re.error:
            return False

    def extract_direct_date_term(
        self,
        normalized_message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        direct_terms = config.get("date_direct_terms", [])

        for term in direct_terms:
            normalized_term = normalize_text(str(term), normalization)

            if normalized_term and normalized_term in normalized_message:
                return str(term)

        return ""


    def extract_explicit_date_phrase(self, raw_message: str, config: Optional[Dict[str, Any]] = None) -> str:
        config = config or {}
        patterns = config.get("explicit_date_patterns", [])

        if not isinstance(patterns, list):
            patterns = []

        for item in patterns:
            pattern = ""
            group = 1

            if isinstance(item, dict):
                pattern = str(item.get("regex", "") or "")
                try:
                    group = int(item.get("group", 1))
                except Exception:
                    group = 1
            else:
                pattern = str(item or "")

            if not pattern:
                continue

            try:
                match = re.search(pattern, raw_message, flags=re.IGNORECASE)
            except re.error:
                continue

            if match:
                try:
                    return match.group(group).strip()
                except Exception:
                    return match.group(0).strip()

        return ""


    def clean_date_text(
        self,
        value: str,
        normalization: Dict[str, Any],
        config: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}
        text = str(value or "").strip()

        if not text:
            return ""

        normalized = normalize_text(text, normalization)

        direct_terms = config.get("date_direct_terms", [])

        if not isinstance(direct_terms, list):
            direct_terms = []

        for term in direct_terms:
            normalized_term = normalize_text(str(term or ""), normalization)
            if normalized_term and normalized_term in normalized:
                return str(term)

        explicit = self.extract_explicit_date_phrase(text, config)

        if explicit:
            return explicit

        cleanup_config = config.get("date_text_cleanup", {})
        if not isinstance(cleanup_config, dict):
            cleanup_config = {}

        strip_prefix_patterns = cleanup_config.get("strip_prefix_patterns", [])
        cut_patterns = cleanup_config.get("cut_patterns", [])

        if isinstance(strip_prefix_patterns, list):
            for pattern in strip_prefix_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue

                try:
                    text = re.sub(pattern_text, "", text).strip()
                except re.error:
                    continue

        if isinstance(cut_patterns, list):
            for pattern in cut_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue

                try:
                    text = re.split(pattern_text, text, maxsplit=1)[0].strip()
                except re.error:
                    continue

        return text

