    def is_ignorable_create_missing(
        self,
        path: str,
        variables: Dict[str, Any],
        config: Dict[str, Any]
    ) -> bool:
        if path == "variables.customer_profile.phone":
            return bool(self.get_existing_phone(variables, config, {}))

        if path == "variables.booking.pending.branch":
            return bool(self.get_known_branch(variables, config))

        if path == "variables.booking.pending.date":
            return bool(
                deep_get(variables, "booking.pending.date")
                or deep_get(variables, "appointment_date")
                or deep_get(variables, "date")
            )

        if path == "variables.booking.pending.date_text":
            return bool(
                deep_get(variables, "booking.pending.date_text")
                or deep_get(variables, "date_text")
                or deep_get(variables, "booking.pending.date")
                or deep_get(variables, "appointment_date")
                or deep_get(variables, "date")
            )

        if path == "variables.booking.pending.time":
            return bool(
                deep_get(variables, "booking.pending.time")
                or deep_get(variables, "appointment_time")
            )

        return False



    def is_hold_or_delay_message(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> bool:
        """
        Detect configured wait/hold messages, such as a customer pausing to
        check documents. All phrases live in domain_bundle.json; this method is
        intentionally domain-neutral and does not embed phrase literals.
        """
        text = str(message or "").strip()
        if not text:
            return False

        phrases: List[str] = []

        direct = config.get("hold_or_delay_phrases", [])
        if isinstance(direct, list):
            phrases.extend([str(item or "") for item in direct])

        policy = config.get("hold_or_delay_response_policy", {})
        if isinstance(policy, dict):
            policy_phrases = policy.get("phrases", []) or policy.get("markers", [])
            if isinstance(policy_phrases, list):
                phrases.extend([str(item or "") for item in policy_phrases])

        markers = config.get("customer_detail_markers", {})
        if isinstance(markers, dict):
            hold_markers = markers.get("hold_or_delay_markers", [])
            if isinstance(hold_markers, list):
                phrases.extend([str(item or "") for item in hold_markers])

        phrases = [phrase for phrase in phrases if str(phrase or "").strip()]

        if not phrases:
            return False

        return matches_any(text, phrases, normalization)


    def deterministic_variant_from_config(
        self,
        variants: Any,
        message: str = ""
    ) -> str:
        """
        Pick a stable configured response variant without randomness.
        The wording itself is configured; Python only selects an item.
        """
        if not isinstance(variants, list):
            return ""

        clean = [str(item or "").strip() for item in variants if str(item or "").strip()]
        if not clean:
            return ""

        seed = sum(ord(ch) for ch in str(message or ""))
        return clean[seed % len(clean)]


    def handle_hold_or_delay_if_needed(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        observations: List[Dict[str, Any]],
        tool_calls_used: int
    ) -> Optional[SubagentResult]:
        """
        If the customer pauses to check documents/details, do not continue
        extraction or booking. Return a configured holding response and preserve
        state. This prevents phrases like "wait while I get the license" from
        being misread as customer or vehicle data.
        """
        policy = config.get("hold_or_delay_response_policy", {})
        if not isinstance(policy, dict):
            policy = {}

        if policy.get("enabled") is False:
            return None

        normalization = context.assistant_config.get("normalization", {})
        if not self.is_hold_or_delay_message(context.user_message, config, normalization):
            return None

        stage_path = config.get("stage_path", "booking.stage")
        current_stage = str(deep_get(variables, stage_path, "") or "")
        active_stages = policy.get("active_stages", [])

        if isinstance(active_stages, list) and active_stages:
            normalized_active = {str(item or "").strip() for item in active_stages if str(item or "").strip()}
            if current_stage not in normalized_active:
                return None

        updates = policy.get("set_updates", {})
        if not isinstance(updates, dict):
            updates = {}

        patched = apply_variable_patch(variables, updates, []) if updates else variables

        answer = self.deterministic_variant_from_config(
            policy.get("response_variants", []),
            context.user_message
        )

        if not answer:
            templates = config.get("templates", {}) if isinstance(config.get("templates", {}), dict) else {}
            template_key = str(policy.get("template_key") or "").strip()
            answer = render_template(
                templates.get(template_key, templates.get("missing_fields", "")),
                {
                    "variables": patched,
                    "message": context.user_message,
                }
            )

        observations.append({
            "subagent": self.name,
            "ok": True,
            "event": "hold_or_delay_detected",
            "stage": current_stage,
        })

        return SubagentResult(
            handled=True,
            action="ask_user",
            answer=answer,
            variable_updates=patched,
            selected_subagent=self.name,
            observations=observations,
            tool_calls_used=tool_calls_used,
            notes="configured hold/delay message detected; booking/extraction paused"
        )


    def get_field_help_match(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Match configured field-help questions. The assistant-specific questions
        and answers live in config.field_help.
        """
        field_help = config.get("field_help", {})
        if not isinstance(field_help, dict):
            return {}

        for field_path, help_config in field_help.items():
            if not isinstance(help_config, dict):
                continue

            phrases = help_config.get("when_user_asks", [])
            if not isinstance(phrases, list) or not phrases:
                continue

            if matches_any(message, phrases, normalization):
                matched = dict(help_config)
                matched["field_path"] = str(field_path or "")
                return matched

        return {}


    def handle_field_help_if_needed(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        observations: List[Dict[str, Any]],
        tool_calls_used: int
    ) -> Optional[SubagentResult]:
        """
        Answer configured meaning/clarification questions, such as asking what a
        required field means, without treating the question as the field value.
        """
        routing = config.get("field_clarification_routing", {})
        if not isinstance(routing, dict):
            routing = {}

        if routing.get("enabled") is False:
            return None

        normalization = context.assistant_config.get("normalization", {})
        match = self.get_field_help_match(context.user_message, config, normalization)
        if not match:
            return None

        stage_path = config.get("stage_path", "booking.stage")
        current_stage = str(deep_get(variables, stage_path, "") or "")
        active_stages = routing.get("during_stages", [])
        if isinstance(active_stages, list) and active_stages:
            normalized_active = {str(item or "").strip() for item in active_stages if str(item or "").strip()}
            if current_stage and current_stage not in normalized_active:
                return None

        answer = self.deterministic_variant_from_config(
            match.get("answer_variants", []),
            context.user_message
        )
        follow_up = self.deterministic_variant_from_config(
            match.get("follow_up_variants", []),
            context.user_message + "follow_up"
        )

        if answer and follow_up:
            answer = f"{answer} {follow_up}".strip()

        if not answer:
            templates = config.get("templates", {}) if isinstance(config.get("templates", {}), dict) else {}
            template_by_field = routing.get("template_by_field", {})
            if not isinstance(template_by_field, dict):
                template_by_field = {}
            template_key = str(
                match.get("template_key")
                or template_by_field.get(str(match.get("field_path") or ""))
                or routing.get("default_template_key")
                or ""
            ).strip()
            answer = render_template(
                templates.get(template_key, templates.get("missing_fields", "")),
                {
                    "variables": variables,
                    "message": context.user_message,
                    "field_help": match,
                }
            )

        observations.append({
            "subagent": self.name,
            "ok": True,
            "event": "field_help_answered",
            "field_path": match.get("field_path", ""),
            "stage": current_stage,
        })

        return SubagentResult(
            handled=True,
            action="ask_user",
            answer=answer,
            variable_updates=variables,
            selected_subagent=self.name,
            observations=observations,
            tool_calls_used=tool_calls_used,
            notes=f"configured field-help answered for {match.get('field_path', '')}"
        )


    def cross_field_rule_matches_value(
        self,
        value: Any,
        rule: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> bool:
        text = normalize_digits(str(value or "").strip(), normalization.get("digit_map", {}))
        if not text:
            return False

        regex = str(
            rule.get("when_value_regex")
            or rule.get("value_regex")
            or rule.get("if_value_regex")
            or ""
        ).strip()
        if regex:
            try:
                if not re.search(regex, text):
                    return False
            except re.error:
                return False

        not_regex = str(
            rule.get("when_value_not_regex")
            or rule.get("not_value_regex")
            or rule.get("if_value_not_regex")
            or ""
        ).strip()
        if not_regex:
            try:
                if re.search(not_regex, text):
                    return False
            except re.error:
                return False

        contains_any = rule.get("when_value_contains_any", [])
        if isinstance(contains_any, list) and contains_any:
            normalized_text = normalize_text(text, normalization)
            if not any(
                normalize_text(str(item or ""), normalization) in normalized_text
                for item in contains_any
                if str(item or "").strip()
            ):
                return False

        return True


    def apply_customer_detail_cross_field_corrections(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Move or clear configured customer-detail values when the value clearly
        belongs to a different configured field.

        Example behavior is entirely config-driven:
        - a year-shaped value captured under class can be moved to model year
        - a class-shaped value captured under model year can be moved to class
        """
        if not isinstance(variables, dict):
            return variables

        rules: List[Dict[str, Any]] = []

        direct_rules = config.get("customer_detail_cross_field_corrections", [])
        if isinstance(direct_rules, list):
            rules.extend([item for item in direct_rules if isinstance(item, dict)])

        vehicle_config = config.get("vehicle_detail_correction", {})
        if isinstance(vehicle_config, dict):
            vehicle_rules = vehicle_config.get("cross_field_corrections", [])
            if isinstance(vehicle_rules, list):
                rules.extend([item for item in vehicle_rules if isinstance(item, dict)])

        if not rules:
            return variables

        updates: Dict[str, Any] = {}
        clear_paths: List[str] = []

        for rule in rules:
            source_path = self.strip_variables_prefix(
                str(rule.get("source_path") or rule.get("from_path") or rule.get("source") or "").strip()
            )
            target_path = self.strip_variables_prefix(
                str(rule.get("target_path") or rule.get("to_path") or rule.get("target") or "").strip()
            )

            if not source_path or not target_path or source_path == target_path:
                continue

            value = deep_get(variables, source_path)
            if value in [None, "", [], {}]:
                continue

            if not self.cross_field_rule_matches_value(value, rule, normalization):
                continue

            target_value = deep_get(variables, target_path)
            overwrite_target = bool(rule.get("overwrite_target", False))
            only_if_target_missing = rule.get("only_if_target_missing", True) is not False

            if overwrite_target or not only_if_target_missing or target_value in [None, "", [], {}]:
                updates[target_path] = self.normalize_cross_field_value(
                    value=value,
                    rule=rule,
                    normalization=normalization
                )

            if rule.get("clear_source", True) is not False:
                clear_paths.append(source_path)

        if not updates and not clear_paths:
            return variables

        return apply_variable_patch(variables, updates, clear_paths)


    def normalize_cross_field_value(
        self,
        value: Any,
        rule: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        text = normalize_digits(str(value or "").strip(), normalization.get("digit_map", {}))

        cleanup_patterns = rule.get("cleanup_patterns", [])
        if isinstance(cleanup_patterns, list):
            for pattern in cleanup_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue
                try:
                    text = re.sub(pattern_text, " ", text).strip()
                except re.error:
                    continue

        return re.sub(r"\s+", " ", text).strip()


    def render_missing_question(
        self,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        missing: List[str],
        message: str
    ) -> str:
        templates = config.get("templates", {})
        if not isinstance(templates, dict):
            templates = {}

        template_key = self.resolve_missing_template_key(config=config, missing=missing)
        labels = config.get("field_labels", {})
        missing_text = format_missing_fields(missing, labels)

        return render_template(
            templates.get(template_key, templates.get("missing_fields", "")),
            {
                "variables": variables,
                "missing_fields": missing_text,
                "missing_paths": missing,
                "message": message
            }
        )

    def resolve_missing_template_key(
        self,
        config: Dict[str, Any],
        missing: List[str]
    ) -> str:
        normalized_missing = sorted([str(item or "").strip() for item in missing if str(item or "").strip()])
        joined_missing = "|".join(normalized_missing)

        by_paths = config.get("missing_template_by_paths", {})
        if isinstance(by_paths, dict):
            for key, template_key in by_paths.items():
                configured = sorted([
                    part.strip()
                    for part in str(key or "").split("|")
                    if part.strip()
                ])

                if configured == normalized_missing:
                    return str(template_key or "missing_fields").strip() or "missing_fields"

        rules = config.get("missing_template_rules", [])
        if isinstance(rules, list):
            for rule in rules:
                if not isinstance(rule, dict):
                    continue

                paths = rule.get("paths", [])
                if not isinstance(paths, list):
                    continue

                configured = sorted([str(item or "").strip() for item in paths if str(item or "").strip()])
                if configured == normalized_missing:
                    return str(rule.get("template_key") or rule.get("template") or "missing_fields").strip() or "missing_fields"

        return str(config.get("default_missing_template_key", "missing_fields") or "missing_fields")

    def extract_customer_details(
        self,
        message: str,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        extracted = extract_by_patterns(
            message=message,
            patterns=config.get("extraction_patterns", []),
            variables=variables,
            normalization_config=normalization
        )

        if not isinstance(extracted, dict):
            extracted = {}

        if self.is_control_only_message(message, config, normalization):
            extracted.pop("customer_profile.full_name", None)

        fallback = self.extract_customer_details_fallback(
            message=message,
            config=config,
            variables=variables,
            normalization=normalization
        )

        for key, value in fallback.items():
            if key not in extracted or extracted.get(key) in [None, "", [], {}]:
                extracted[key] = value

        if "customer_profile.full_name" in extracted:
            full_name = str(extracted.get("customer_profile.full_name") or "").strip()

            if (
                self.is_control_only_message(message, config, normalization)
                or not self.message_has_name_signal(message, config, normalization)
                or not self.looks_like_person_name(full_name, config, normalization)
            ):
                extracted.pop("customer_profile.full_name", None)

        if not extracted:
            return {}

        return self.post_process_extracted_fields(extracted, config, normalization)


    def extract_customer_details_fallback(
        self,
        message: str,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Extract configured customer details without domain-specific field logic.

        The subagent does not know what details a booking domain requires.
        It reads the detail definitions from config and writes values only to
        each field's configured target_path. Existing legacy phone/name helpers
        remain as generic convenience fallbacks, but every additional domain
        field is discovered from required_before_create,
        customer_profile_persistence.fields, customer_detail_fields, or
        required_customer_details.
        """
        output: Dict[str, Any] = {}
        text = str(message or "").strip()

        if self.is_hold_or_delay_message(text, config, normalization):
            return {}

        if self.get_field_help_match(text, config, normalization):
            return {}

        # Generic phone reuse/extraction remains because "phone" is a common
        # contact field, not an assistant-specific booking object.
        phone_config = config.get("phone_extraction", {})
        phone_regex = phone_config.get("regex", r"(01\d{9}|\+?20\d{10,11})")

        if self.is_same_phone_request(text, config, normalization):
            existing_phone = self.get_existing_phone(variables, config, normalization)
            if existing_phone:
                output["customer_profile.phone"] = existing_phone

        if not deep_get(variables, "customer_profile.phone") and not output.get("customer_profile.phone"):
            try:
                phone_match = re.search(
                    phone_regex,
                    normalize_digits(text, normalization.get("digit_map", {}))
                )
            except re.error:
                phone_match = None

            if phone_match:
                output["customer_profile.phone"] = phone_match.group(1).strip()

        # Generic person-name extraction remains because it is a common customer
        # identity field. It is still gated by configured name markers/patterns.
        if (
            not deep_get(variables, "customer_profile.full_name")
            and self.message_has_name_signal(text, config, normalization)
            and not self.is_control_only_message(text, config, normalization)
        ):
            name = self.extract_name_from_configured_patterns(
                message=text,
                config=config
            )

            if name and self.looks_like_person_name(name, config, normalization):
                output["customer_profile.full_name"] = name

        # Domain-specific customer details are config-driven.
        for field_config in self.get_customer_detail_field_configs(config):
            target_path = str(field_config.get("target_path") or "").strip()

            if not target_path:
                continue

            if target_path in output:
                continue

            if deep_get(variables, target_path) not in [None, "", [], {}]:
                continue

            value = self.extract_configured_customer_detail_field(
                message=text,
                field_config=field_config,
                config=config,
                normalization=normalization
            )

            if value not in [None, "", [], {}]:
                output[target_path] = value

        return output


    def capture_missing_required_paths_from_config_patterns(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        message: str
    ) -> Dict[str, Any]:
        """
        Deterministic last-mile capture for missing required create fields.

        This intentionally does not depend on customer_detail_fields being
        merged correctly. It reads required_before_create, finds currently
        missing required paths, then runs only config.extraction_patterns that
        explicitly target that missing path.

        No domain-specific field names or phrases are hardcoded here.
        """
        if not isinstance(variables, dict):
            variables = {}

        text = str(message or "").strip()
        if not text:
            return variables

        patterns = config.get("extraction_patterns", [])
        if not isinstance(patterns, list) or not patterns:
            return variables

        missing_paths = self.get_missing_required_create_fields(config, variables)
        missing_clean = [
            self.strip_variables_prefix(str(path or "").strip())
            for path in missing_paths
            if str(path or "").strip()
        ]

        if not missing_clean:
            return variables

        field_configs_by_path: Dict[str, Dict[str, Any]] = {}
        for field_config in self.get_customer_detail_field_configs(config):
            target_path = self.strip_variables_prefix(str(field_config.get("target_path") or "").strip())
            if target_path:
                field_configs_by_path[target_path] = field_config

        updates: Dict[str, Any] = {}

        for missing_path in missing_clean:
            if deep_get(variables, missing_path) not in [None, "", [], {}]:
                continue

            value = self.extract_missing_required_path_from_config_patterns(
                message=text,
                target_path=missing_path,
                patterns=patterns,
                field_config=field_configs_by_path.get(missing_path, {}),
                config=config,
                normalization=normalization
            )

            if not value:
                continue

            updates[missing_path] = value

            field_config = field_configs_by_path.get(missing_path, {})
            backup_paths = field_config.get("backup_paths", []) if isinstance(field_config, dict) else []
            if isinstance(backup_paths, list):
                for backup_path in backup_paths:
                    backup_clean = self.strip_variables_prefix(str(backup_path or "").strip())
                    if backup_clean:
                        updates[backup_clean] = value

            # Generic mirror for customer_profile.* fields when no explicit
            # backup path is configured or loaded.
            if missing_path.startswith("customer_profile."):
                field_name = missing_path.split(".", 1)[1].strip()
                if field_name:
                    updates.setdefault(f"booking.customer_profile.{field_name}", value)

        if not updates:
            return variables

        return apply_variable_patch(variables, updates, [])


    def extract_missing_required_path_from_config_patterns(
        self,
        message: str,
        target_path: str,
        patterns: List[Any],
        field_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        clean_target = self.strip_variables_prefix(str(target_path or "").strip())

        if not clean_target:
            return ""

        digit_map = normalization.get("digit_map", {})
        raw_message = str(message or "")
        digit_message = normalize_digits(raw_message, digit_map)
        normalized_message = normalize_text(digit_message, normalization)

        candidates: List[Dict[str, Any]] = []

        for item in patterns:
            if not isinstance(item, dict):
                continue

            item_target = self.strip_variables_prefix(
                str(
                    item.get("variable")
                    or item.get("path")
                    or item.get("target_path")
                    or ""
                ).strip()
            )
            when_missing = self.strip_variables_prefix(str(item.get("when_missing") or "").strip())

            if item_target != clean_target and when_missing != clean_target:
                continue

            pattern_text = str(item.get("regex") or item.get("pattern") or "").strip()
            if not pattern_text:
                continue

            try:
                group_index = int(item.get("group", 1))
            except Exception:
                group_index = 1

            for haystack, source in [
                (raw_message, "required_pattern_raw"),
                (digit_message, "required_pattern_digits"),
                (normalized_message, "required_pattern_normalized"),
            ]:
                if not haystack:
                    continue

                try:
                    matches = list(re.finditer(pattern_text, haystack, flags=re.IGNORECASE))
                except re.error:
                    continue

                for match in matches:
                    try:
                        candidate = (
                            match.group(group_index).strip()
                            if match.groups()
                            else match.group(0).strip()
                        )
                    except Exception:
                        candidate = match.group(0).strip()

                    value = self.normalize_missing_required_pattern_value(
                        candidate=candidate,
                        item=item,
                        field_config=field_config,
                        config=config,
                        normalization=normalization
                    )

                    if value:
                        candidates.append({
                            "value": value,
                            "raw": candidate,
                            "source": source
                        })

        if not candidates:
            return ""

        candidates.sort(
            key=lambda item: self.score_configured_customer_detail_candidate(
                value=str(item.get("value") or ""),
                raw=str(item.get("raw") or ""),
                source=str(item.get("source") or ""),
                field_config=field_config if isinstance(field_config, dict) else {},
                normalization=normalization
            ),
            reverse=True
        )

        return str(candidates[0].get("value") or "").strip()


    def normalize_missing_required_pattern_value(
        self,
        candidate: Any,
        item: Dict[str, Any],
        field_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        text = normalize_digits(str(candidate or "").strip(), normalization.get("digit_map", {}))

        if not text:
            return ""

        if isinstance(field_config, dict) and field_config:
            value = self.normalize_customer_profile_value(
                value=text,
                validator=str(field_config.get("validator") or ""),
                config=config,
                normalization=normalization,
                field_config=field_config
            )

            if value:
                return value

            # Fall through to item-level loose validation because strict field
            # regexes can be too conservative even after an explicit configured
            # extraction pattern matched the missing required path.

        cleanup_patterns = item.get("cleanup_patterns", [])
        if isinstance(cleanup_patterns, list):
            for pattern in cleanup_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue
                try:
                    text = re.sub(pattern_text, " ", text).strip()
                except re.error:
                    continue

        separator_pattern = str(item.get("separator_pattern", r"[،,.!?؟:;]+") or "")
        if separator_pattern:
            try:
                text = re.sub(separator_pattern, " ", text)
            except re.error:
                pass

        text = re.sub(r"\s+", " ", text).strip()

        if not text:
            return ""

        # Generic item-level safeguards. These are config-driven where present
        # and intentionally light when the regex itself already targeted the
        # required missing path.
        if item.get("require_digit") is True and not re.search(r"\d", text):
            return ""

        if item.get("require_letter") is True and not re.search(r"[A-Za-z\u0600-\u06FF]", text):
            return ""

        if item.get("require_letter_or_digit") is True and not re.search(r"[A-Za-z0-9\u0600-\u06FF]", text):
            return ""

        try:
            min_length = int(item.get("min_length", 1) or 1)
        except Exception:
            min_length = 1

        try:
            max_length = int(item.get("max_length", 300) or 300)
        except Exception:
            max_length = 300

        if len(text) < min_length or len(text) > max_length:
            return ""

        return text


    def capture_missing_configured_details_from_current_message(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        message: str
    ) -> Dict[str, Any]:
        """
        Last-mile generic customer-detail capture.

        This runs after normal extraction and before missing-field/create checks.
        It only looks at configured customer_detail_fields / required paths and
        configured markers/extraction rules. It does not know any domain field
        names, labels, or phrases.

        Purpose:
        - If the current message contains a missing configured detail, persist it
          to its configured target_path and backup_paths immediately.
        - Prevent the response layer from acknowledging a detail that never
          reached state.
        """
        if not isinstance(variables, dict):
            variables = {}

        text = str(message or "").strip()
        if not text:
            return variables

        updates: Dict[str, Any] = {}

        missing_paths = self.get_missing_required_create_fields(config, variables)
        missing_clean = {
            self.strip_variables_prefix(str(path or "").strip())
            for path in missing_paths
            if str(path or "").strip()
        }

        for field_config in self.get_customer_detail_field_configs(config):
            target_path = str(field_config.get("target_path") or "").strip()

            if not target_path:
                continue

            clean_target_path = self.strip_variables_prefix(target_path)
            existing_value = deep_get(variables, clean_target_path)

            # Only force-capture missing/empty configured details. This prevents
            # accidental overwrite of already known customer details.
            if existing_value not in [None, "", [], {}]:
                continue

            if missing_clean and clean_target_path not in missing_clean:
                # If the field is not required/missing, only capture it when the
                # field marker is explicitly present. This keeps optional details safe.
                if not self.current_message_has_field_marker(
                    message=text,
                    field_config=field_config,
                    normalization=normalization
                ):
                    continue

            value = self.extract_required_detail_from_config_patterns(
                message=text,
                target_path=clean_target_path,
                field_config=field_config,
                config=config,
                normalization=normalization
            )

            if not value:
                value = self.extract_configured_customer_detail_field(
                    message=text,
                    field_config=field_config,
                    config=config,
                    normalization=normalization
                )

            if not value:
                value = self.extract_configured_detail_by_marker_remainder(
                    message=text,
                    field_config=field_config,
                    config=config,
                    normalization=normalization
                )

            if not value:
                value = self.extract_configured_detail_by_explicit_marker_loose(
                    message=text,
                    field_config=field_config,
                    config=config,
                    normalization=normalization
                )

            if not value:
                continue

            updates[clean_target_path] = value

            backup_paths = field_config.get("backup_paths", [])
            if isinstance(backup_paths, list):
                for backup_path in backup_paths:
                    backup_text = self.strip_variables_prefix(str(backup_path or "").strip())
                    if backup_text:
                        updates[backup_text] = value

        if not updates:
            return variables

        return apply_variable_patch(variables, updates, [])


    def current_message_has_field_marker(
        self,
        message: str,
        field_config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> bool:
        normalized_message = normalize_text(str(message or ""), normalization)

        markers = field_config.get("markers", [])
        if not isinstance(markers, list):
            markers = []

        for marker in markers:
            marker_text = str(marker or "").strip()
            if not marker_text:
                continue

            normalized_marker = normalize_text(marker_text, normalization)
            if normalized_marker and normalized_marker in normalized_message:
                return True

        return False


    def extract_configured_detail_by_marker_remainder(
        self,
        message: str,
        field_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        """
        Direct configured-marker capture fallback.

        This version deliberately searches the NORMALIZED message and NORMALIZED
        markers. That fixes Arabic spelling variants such as ة/ه, أ/ا, and
        digit forms without hardcoding any domain phrase in Python.
        """
        candidates = self.extract_normalized_marker_remainder_candidates(
            message=message,
            field_config=field_config,
            config=config,
            normalization=normalization,
            source="direct_marker_remainder"
        )

        if not candidates:
            return ""

        candidates.sort(
            key=lambda item: self.score_configured_customer_detail_candidate(
                value=str(item.get("value") or ""),
                raw=str(item.get("raw") or ""),
                source=str(item.get("source") or ""),
                field_config=field_config,
                normalization=normalization
            ),
            reverse=True
        )

        return str(candidates[0].get("value") or "").strip()


    def extract_normalized_marker_remainder_candidates(
        self,
        message: str,
        field_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        source: str = "normalized_marker_remainder"
    ) -> List[Dict[str, Any]]:
        extraction_config = field_config.get("extraction", {})
        if not isinstance(extraction_config, dict):
            extraction_config = {}

        markers = field_config.get("markers", [])
        if not isinstance(markers, list):
            markers = []

        digit_map = normalization.get("digit_map", {})
        raw_message = normalize_digits(str(message or ""), digit_map)
        normalized_message = normalize_text(raw_message, normalization)

        if not normalized_message:
            return []

        try:
            max_chars = int(
                extraction_config.get(
                    "marker_capture_max_chars",
                    extraction_config.get("max_length", 80)
                ) or 80
            )
        except Exception:
            max_chars = 80

        max_chars = max(10, min(max_chars, 300))

        stop_pattern = str(
            extraction_config.get("marker_capture_stop_pattern")
            or r"[\n،,.!?؟]+"
        )

        output: List[Dict[str, Any]] = []

        for marker in markers:
            marker_text = normalize_digits(str(marker or "").strip(), digit_map)
            normalized_marker = normalize_text(marker_text, normalization)

            if not normalized_marker:
                continue

            marker_index = normalized_message.find(normalized_marker)

            if marker_index < 0:
                continue

            remainder = normalized_message[marker_index + len(normalized_marker):]
            remainder = re.sub(r"^\s*[:：\-–—ـ]*\s*", "", remainder).strip()

            if not remainder:
                continue

            try:
                stop_match = re.search(stop_pattern, remainder)
            except re.error:
                stop_match = None

            if stop_match:
                remainder = remainder[:stop_match.start()].strip()

            if len(remainder) > max_chars:
                remainder = remainder[:max_chars].strip()

            normalized_value = self.normalize_customer_profile_value(
                value=remainder,
                validator=str(field_config.get("validator") or ""),
                config=config,
                normalization=normalization,
                field_config=field_config
            )

            if normalized_value:
                output.append({
                    "value": normalized_value,
                    "source": source,
                    "raw": remainder
                })

        return output


    def extract_required_detail_from_config_patterns(
        self,
        message: str,
        target_path: str,
        field_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        """
        Directly run configured extraction_patterns for the specific missing
        required customer detail path.

        This is a last-mile deterministic capture that does NOT know any
        domain-specific field names. It only uses:
        - required_before_create target_path
        - config.extraction_patterns item.variable / item.path / item.target_path
        - field_config validator/extraction safety rules

        It fixes cases where the LLM response sees the detail but the state
        stays empty because a generic route-specific extractor missed the field.
        """
        clean_target = self.strip_variables_prefix(str(target_path or "").strip())
        if not clean_target:
            return ""

        patterns = config.get("extraction_patterns", [])
