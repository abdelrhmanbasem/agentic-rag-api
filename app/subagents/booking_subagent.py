    def post_process_extracted_fields(
        self,
        extracted: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        output = dict(extracted)

        plate = output.get("customer_profile.plate_number")

        if plate:
            plate_text = self.normalize_plate_number(
                str(plate),
                normalization
            )

            if self.looks_like_plate_number(plate_text, config, normalization):
                output["customer_profile.plate_number"] = plate_text
            else:
                output.pop("customer_profile.plate_number", None)

        if (
            output.get("customer_profile.full_name") in [None, "", [], {}]
            and output.get("customer_profile.name") not in [None, "", [], {}]
        ):
            output["customer_profile.full_name"] = output.get("customer_profile.name")

        full_name = output.get("customer_profile.full_name")

        if full_name:
            clean = self.clean_name(str(full_name), config)

            if self.looks_like_person_name(clean, config, normalization):
                output["customer_profile.full_name"] = re.sub(
                    r"\s+",
                    " ",
                    clean.strip()
                )
            else:
                output.pop("customer_profile.full_name", None)

        phone = output.get("customer_profile.phone")

        if phone:
            output["customer_profile.phone"] = normalize_digits(
                str(phone).strip(),
                normalization.get("digit_map", {})
            )

        return output

    @staticmethod
    def norm_section(value: str) -> str:
        return re.sub(r"\s+", " ", str(value or "").strip().lower())

    @staticmethod
    def normalize_plate_number(value: str, normalization: Dict[str, Any]) -> str:
        digit_map = normalization.get("digit_map", {})
        text = str(value or "")

        for src, dst in digit_map.items():
            text = text.replace(src, dst)

        text = re.sub(r"[،,.!?؟:;]+", " ", text)
        text = re.sub(r"\s+", " ", text).strip()

        return text
