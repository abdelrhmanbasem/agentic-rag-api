    def ensure_selected_branch_from_context(
        self,
        variables: Dict[str, Any],
        assistant_config: Dict[str, Any],
        message: str,
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        selected = str(deep_get(variables, "selected_branch") or "").strip()

        canonical_selected = self.resolve_canonical_branch(
            value=selected,
            assistant_config=assistant_config,
            normalization=normalization
        )

        if canonical_selected:
            if canonical_selected != selected:
                return apply_variable_patch(
                    variables,
                    {
                        "selected_branch": canonical_selected,
                        "location_branch": canonical_selected
                    },
                    []
                )
            return variables

        pending_branch = str(deep_get(variables, "booking.pending.branch") or "").strip()

        canonical_pending = self.resolve_canonical_branch(
            value=pending_branch,
            assistant_config=assistant_config,
            normalization=normalization
        )

        if canonical_pending:
            return apply_variable_patch(
                variables,
                {
                    "selected_branch": canonical_pending,
                    "location_branch": canonical_pending
                },
                []
            )

        message_branch = self.resolve_canonical_branch(
            value=message,
            assistant_config=assistant_config,
            normalization=normalization
        )

        if message_branch:
            return apply_variable_patch(
                variables,
                {
                    "selected_branch": message_branch,
                    "location_branch": message_branch
                },
                []
            )

        known_branch = self.get_known_branch(variables, get_subagent_config(assistant_config, self.name))

        if known_branch:
            return apply_variable_patch(
                variables,
                {
                    "selected_branch": known_branch,
                    "location_branch": known_branch
                },
                []
            )

        inferred_branch = self.infer_branch_from_available_branches(
            variables=variables,
            message=message,
            normalization=normalization,
            config=get_subagent_config(assistant_config, self.name)
        )

        if inferred_branch:
            return apply_variable_patch(
                variables,
                {
                    "selected_branch": inferred_branch,
                    "location_branch": inferred_branch,
                    "nearest_branch": inferred_branch
                },
                []
            )

        return variables


    def get_known_branch(
        self,
        variables: Dict[str, Any],
        config: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}

        paths: List[str] = []

        carryover = config.get("slot_context_carryover", {})
        if isinstance(carryover, dict):
            values = carryover.get("never_ask_branch_if_any_exist", [])
            if isinstance(values, list):
                paths.extend([str(item).replace("variables.", "") for item in values if str(item or "").strip()])

            values = carryover.get("known_branch_fallback_order", [])
            if isinstance(values, list):
                paths.extend([
                    str(item).replace("variables.", "").replace("slot.", "")
                    for item in values
                    if str(item or "").strip()
                ])

        configured_paths = config.get("known_branch_paths", [])
        if isinstance(configured_paths, list):
            paths.extend([str(item).replace("variables.", "") for item in configured_paths if str(item or "").strip()])

        seen = set()
        ordered_paths = []
        for path in paths:
            path = str(path or "").strip()
            if path and path not in seen:
                ordered_paths.append(path)
                seen.add(path)

        for path in ordered_paths:
            value = str(deep_get(variables, path) or "").strip()
            if value:
                return value

        return ""

    def ensure_pending_booking_context(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        assistant_config: Dict[str, Any],
        message: str,
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        pending_path = config.get("pending_booking_path", "booking.pending")
        pending = deep_get(variables, pending_path, {})

        if not isinstance(pending, dict):
            pending = {}

        updates: Dict[str, Any] = {}
        patched_pending = dict(pending)

        branch = (
            patched_pending.get("branch")
            or self.get_known_branch(variables, config)
            or self.infer_branch_from_available_branches(
                variables=variables,
                message=message,
                normalization=normalization,
                config=config
            )
        )

        if branch:
            canonical_branch = self.resolve_canonical_branch(
                value=str(branch),
                assistant_config=assistant_config,
                normalization=normalization
            ) or str(branch)

            patched_pending["branch"] = canonical_branch
            updates["selected_branch"] = canonical_branch
            updates["location_branch"] = canonical_branch

            if not deep_get(variables, "nearest_branch"):
                updates["nearest_branch"] = canonical_branch

        date_value = (
            patched_pending.get("date")
            or deep_get(variables, config.get("appointment_date_path", "appointment_date"))
            or deep_get(variables, "date")
        )

        if date_value:
            patched_pending["date"] = date_value
            updates[config.get("appointment_date_path", "appointment_date")] = date_value
            updates["date"] = date_value

        date_text = (
            patched_pending.get("date_text")
            or deep_get(variables, config.get("date_text_path", "date_text"))
            or self.first_non_empty_from_paths(
                variables=variables,
                paths=self.get_configured_path_list(
                    config=config,
                    key_path="pending_date_text_fallback_paths",
                    default=[
                        config.get("appointment_date_path", "appointment_date"),
                        "date",
                        "booking.pending.date"
                    ]
                )
            )
        )

        if date_text:
            patched_pending["date_text"] = date_text
            updates[config.get("date_text_path", "date_text")] = date_text

        time_value = patched_pending.get("time") or deep_get(variables, "appointment_time")

        if time_value:
            patched_pending["time"] = time_value
            updates["appointment_time"] = time_value

        section = patched_pending.get("section") or deep_get(variables, "service_needed")

        if section and not patched_pending.get("section"):
            patched_pending["section"] = section

        if patched_pending and patched_pending != pending:
            updates[pending_path] = patched_pending
            updates["booking.pending_summary"] = self.build_pending_summary(patched_pending)

        if not updates:
            return variables

        return apply_variable_patch(variables, updates, [])


    def infer_branch_from_available_branches(
        self,
        variables: Dict[str, Any],
        message: str,
        normalization: Dict[str, Any],
        config: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}
        branches = deep_get(variables, "available_branches", [])

        if not isinstance(branches, list) or not branches:
            return ""

        location_text = " ".join([
            str(deep_get(variables, "user_area") or ""),
            str(message or "")
        ]).strip()

        if not location_text:
            return ""

        location_norm = normalize_text(location_text, normalization)
        location_tokens = self.extract_meaningful_tokens(
            text=location_norm,
            config=config,
            normalization=normalization
        )

        if not location_tokens:
            return ""

        best_branch = ""
        best_score = 0

        for branch_item in branches:
            if not isinstance(branch_item, dict):
                continue

            branch_name = str(branch_item.get("branch") or "").strip()
            branch_area = str(branch_item.get("area") or "").strip()

            if not branch_name or not branch_area:
                continue

            area_norm = normalize_text(branch_area, normalization)
            score = 0

            for token in location_tokens:
                if token and token in area_norm:
                    score += 1

            if score > best_score:
                best_score = score
                best_branch = branch_name

        if best_score > 0:
            return best_branch

        return ""


    @staticmethod
    def extract_meaningful_tokens(
        text: str,
        config: Optional[Dict[str, Any]] = None,
        normalization: Optional[Dict[str, Any]] = None
    ) -> List[str]:
        config = config or {}
        normalization = normalization or {}
        raw_tokens = re.findall(r"[\w\u0600-\u06FF]+", str(text or "").lower())

        stopwords = config.get("branch_inference_stopwords", [])

        if not isinstance(stopwords, list):
            stopwords = []

        normalized_stopwords = {
            normalize_text(str(item or ""), normalization)
            for item in stopwords
            if str(item or "").strip()
        }

        max_tokens = int(config.get("branch_inference_max_tokens", 12) or 12)
        min_token_length = int(config.get("branch_inference_min_token_length", 3) or 3)

        output = []

        for token in raw_tokens:
            token = token.strip()

            if not token:
                continue

            normalized_token = normalize_text(token, normalization)

            if normalized_token in normalized_stopwords:
                continue

            if len(token) < min_token_length:
                continue

            output.append(token)

        return output[:max_tokens]

    def resolve_canonical_branch(
        self,
        value: str,
        assistant_config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        text = str(value or "").strip()

        if not text:
            return ""

        aliases = assistant_config.get("branch_aliases", {})

        if not isinstance(aliases, dict):
            return ""

        normalized_text = normalize_text(text, normalization)

        for canonical, alias_list in aliases.items():
            canonical_text = str(canonical or "").strip()

            if not canonical_text:
                continue

            candidates = [canonical_text]

            if isinstance(alias_list, list):
                candidates.extend([str(item or "") for item in alias_list])

            for candidate in candidates:
                normalized_candidate = normalize_text(candidate, normalization)

                if not normalized_candidate:
                    continue

                if normalized_candidate == normalized_text:
                    return canonical_text

                if normalized_candidate in normalized_text:
                    return canonical_text

        return ""

    def is_booking_or_availability_request(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any]
    ) -> bool:
        normalization = context.assistant_config.get("normalization", {})

        if self.is_date_only_question(context.user_message, config, normalization):
            return False

        phrases = config.get("trigger_phrases", [])
        availability_terms = config.get("availability_request_terms", [])

        has_trigger_intent = matches_any(context.user_message, phrases, normalization)
        has_availability_intent = matches_any(context.user_message, availability_terms, normalization)

        if has_trigger_intent:
            return True

        date_text = self.extract_date_text(context.user_message, config, normalization)
        stage_path = config.get("stage_path", "booking.stage")
        stage = deep_get(variables, stage_path, "")

        date_only_request_stages = config.get("date_only_booking_request_stages", [
            "awaiting_date",
            "needs_date"
        ])

        if (
            date_text
            and self.get_known_branch(variables, config)
            and (
                has_availability_intent
                or stage in date_only_request_stages
            )
        ):
            return True

        if stage in config.get("active_request_stages", []):
            return bool(has_availability_intent or date_text)

        return False

    def is_new_availability_request(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any]
    ) -> bool:
        normalization = context.assistant_config.get("normalization", {})
        message = normalize_text(context.user_message, normalization)

        availability_terms = config.get("availability_request_terms", [])

        if not isinstance(availability_terms, list):
            availability_terms = []

        has_availability_intent = any(
            normalize_text(term, normalization) in message
            for term in availability_terms
            if str(term or "").strip()
        )

        date_text = self.extract_date_text(context.user_message, config, normalization)

        if not has_availability_intent and not date_text:
            return False

        branch = self.get_known_branch(variables, config)
        appointment_date = (
            deep_get(variables, config.get("appointment_date_path", "appointment_date"))
            or deep_get(variables, "date")
        )

        return bool(branch and (appointment_date or date_text))

