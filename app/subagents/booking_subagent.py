    def handle_slot_selected(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        selected_slot: Dict[str, Any]
    ) -> SubagentResult:
        normalization = context.assistant_config.get("normalization", {})

        variables = self.ensure_selected_branch_from_context(
            variables=variables,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        slot_mapping = config.get("slot_to_pending_booking_mapping", {})
        pending_booking_path = config.get("pending_booking_path", "booking.pending")

        pending = build_object_from_mapping(slot_mapping, {
            "variables": variables,
            "slot": selected_slot,
            "message": context.user_message
        })

        known_branch = self.get_known_branch(variables)

        if pending.get("branch") in [None, ""]:
            pending["branch"] = known_branch

        if pending.get("branch") not in [None, ""]:
            canonical_branch = self.resolve_canonical_branch(
                value=str(pending.get("branch") or ""),
                assistant_config=context.assistant_config,
                normalization=normalization
            )
            if canonical_branch:
                pending["branch"] = canonical_branch

        if pending.get("date") in [None, ""]:
            pending["date"] = (
                deep_get(variables, config.get("appointment_date_path", "appointment_date"))
                or deep_get(variables, "date")
                or pending.get("date_text")
            )

        if pending.get("date_text") in [None, ""]:
            pending["date_text"] = deep_get(variables, config.get("date_text_path", "date_text"), "")

        if pending.get("time") in [None, ""]:
            selected_time = selected_slot.get("time") if isinstance(selected_slot, dict) else ""
            if selected_time:
                pending["time"] = selected_time

        if pending.get("section") in [None, ""]:
            selected_section = selected_slot.get("section") if isinstance(selected_slot, dict) else ""
            if selected_section:
                pending["section"] = selected_section

        updates = dict(config.get("on_slot_selected_updates", {}))
        updates[pending_booking_path] = pending
        updates["booking.pending_summary"] = self.build_pending_summary(pending)

        if pending.get("branch"):
            updates["selected_branch"] = pending.get("branch")
            updates["location_branch"] = pending.get("branch")

            if not deep_get(variables, "nearest_branch"):
                updates["nearest_branch"] = pending.get("branch")

        if pending.get("date"):
            updates[config.get("appointment_date_path", "appointment_date")] = pending.get("date")
            updates["date"] = pending.get("date")

        if pending.get("date_text"):
            updates[config.get("date_text_path", "date_text")] = pending.get("date_text")

        if pending.get("time"):
            updates["appointment_time"] = pending.get("time")

        patched = apply_variable_patch(variables, updates, [])

        answer = render_template(
            config.get("templates", {}).get("confirm_slot", "BOOKING_CONFIRM_SELECTED_SLOT"),
            {
                "variables": patched,
                "slot": selected_slot,
                "pending": pending,
                "message": context.user_message
            }
        )

        return SubagentResult(
            handled=True,
            action="ask_user",
            answer=answer,
            variable_updates=patched,
            clear_variables=config.get("on_slot_selected_clear", []),
            selected_subagent=self.name,
            notes="slot selected; pending booking prepared with branch/date/time context"
        )


    def handle_booking_request(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        observations: List[Dict[str, Any]],
        tool_calls_used: int
    ) -> SubagentResult:
        if self.is_booking_completed(variables):
            answer = render_template(
                config.get("templates", {}).get("booking_already_confirmed", "BOOKING_ALREADY_CONFIRMED"),
                {
                    "variables": variables,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="reply",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes="booking already confirmed; new booking request blocked"
            )

        normalization = context.assistant_config.get("normalization", {})

        variables = self.ensure_selected_branch_from_context(
            variables=variables,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        variables = self.ensure_resolved_date_from_existing_state(
            variables=variables,
            config=config,
            normalization=normalization,
            message=context.user_message
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=normalization
        )

        required_for_slots = config.get("required_before_list_slots", [])
        missing = get_missing_paths(required_for_slots, variables)

        if missing:
            variables = apply_variable_patch(
                variables,
                config.get("on_missing_list_slots_updates", {}),
                []
            )

            answer = self.render_missing_question(config, variables, missing, context.user_message)

            return SubagentResult(
                handled=True,
                action="ask_user",
                answer=answer,
                variable_updates=variables,
                selected_subagent=self.name,
                observations=observations,
                tool_calls_used=tool_calls_used,
                notes=f"booking request missing required inputs: {missing}"
            )

        operations = config.get("operations", {})
        tool_name = config.get("tool_name", "")
        operation = operations.get("list_slots", "")
        arguments_mapping = config.get("list_slots_arguments", {})

        arguments = build_object_from_mapping(arguments_mapping, {
            "variables": variables,
            "message": context.user_message
        })

        appointment_date = (
            deep_get(variables, "booking.pending.date")
            or deep_get(variables, config.get("appointment_date_path", "appointment_date"))
            or deep_get(variables, "date")
        )

        date_text = (
            deep_get(variables, "booking.pending.date_text")
            or deep_get(variables, config.get("date_text_path", "date_text"))
        )

        branch = self.resolve_canonical_branch(
            value=str(arguments.get("branch") or ""),
            assistant_config=context.assistant_config,
            normalization=normalization
        ) or self.get_known_branch(variables)

        if branch:
            arguments["branch"] = branch

        if appointment_date:
            arguments["date"] = appointment_date

        if date_text and not arguments.get("date_text"):
            arguments["date_text"] = date_text

        tool_result = context.tool_runner.call(
            tool_name=tool_name,
            operation=operation,
            arguments=compact_dict(arguments)
        )

        tool_result = self.filter_slots_by_service_needed(
            tool_result=tool_result,
            variables=variables
        )

        observations.append({
            "subagent": self.name,
            "operation": operation,
            "arguments": arguments,
            "result": tool_result
        })

        tool_calls_used += 1

        updated_variables = apply_tool_update_rules(
            assistant_config=context.assistant_config,
            variables=variables,
            operation=operation,
            arguments=arguments,
            result=tool_result
        )

        repair_updates: Dict[str, Any] = {}

        if branch:
            repair_updates["selected_branch"] = branch
            repair_updates["location_branch"] = branch

        if appointment_date:
            repair_updates[config.get("appointment_date_path", "appointment_date")] = appointment_date
            repair_updates["date"] = appointment_date

        if date_text:
            repair_updates[config.get("date_text_path", "date_text")] = date_text

        if repair_updates:
            updated_variables = apply_variable_patch(updated_variables, repair_updates, [])

        result_context = {
            "variables": updated_variables,
            "result": tool_result,
            "arguments": arguments,
            "message": context.user_message
        }

        slots_found_path = config.get("slots_found_result_path", "slots_found")
        slots_found = deep_get(tool_result, slots_found_path, False)

        if tool_result.get("ok") is False:
            template = config.get("templates", {}).get("tool_error", "BOOKING_TOOL_ERROR")
        elif slots_found is True:
            template = config.get("templates", {}).get("slots_found", "BOOKING_SLOTS_FOUND")
        else:
            template = config.get("templates", {}).get("no_slots", "BOOKING_NO_SLOTS")

        answer = render_template(template, result_context)

        return SubagentResult(
            handled=True,
            action="reply",
            answer=answer,
            variable_updates=updated_variables,
            observations=observations,
            selected_subagent=self.name,
            tool_calls_used=tool_calls_used,
            notes="listed slots"
        )

    def call_create_booking(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        observations: List[Dict[str, Any]],
        tool_calls_used: int
    ) -> SubagentResult:
        if self.is_booking_completed(variables):
            answer = render_template(
                config.get("templates", {}).get("booking_already_confirmed", "BOOKING_ALREADY_CONFIRMED"),
                {
                    "variables": variables,
                    "message": context.user_message
                }
            )

            return SubagentResult(
                handled=True,
                action="reply",
                answer=answer,
                variable_updates=variables,
                observations=observations,
                selected_subagent=self.name,
                tool_calls_used=tool_calls_used,
                notes="create_booking blocked because booking already confirmed"
            )

        variables = self.ensure_resolved_date_from_existing_state(
            variables=variables,
            config=config,
            normalization=context.assistant_config.get("normalization", {}),
            message=context.user_message
        )

        variables = self.ensure_selected_branch_from_context(
            variables=variables,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=context.assistant_config.get("normalization", {})
        )

        variables = self.ensure_pending_booking_context(
            variables=variables,
            config=config,
            assistant_config=context.assistant_config,
            message=context.user_message,
            normalization=context.assistant_config.get("normalization", {})
        )

        operations = config.get("operations", {})
        tool_name = config.get("tool_name", "")
        operation = operations.get("create_booking", "")
        arguments_mapping = config.get("create_booking_arguments", {})

        arguments = build_object_from_mapping(arguments_mapping, {
            "variables": variables,
            "message": context.user_message
        })

        appointment_date = (
            deep_get(variables, "booking.pending.date")
            or deep_get(variables, config.get("appointment_date_path", "appointment_date"))
            or deep_get(variables, "date")
        )

        date_text = (
            deep_get(variables, "booking.pending.date_text")
            or deep_get(variables, config.get("date_text_path", "date_text"))
        )

        if appointment_date:
            arguments["date"] = appointment_date

        if date_text and not arguments.get("date_text"):
            arguments["date_text"] = date_text

        branch = self.resolve_canonical_branch(
            value=str(arguments.get("branch") or ""),
            assistant_config=context.assistant_config,
            normalization=context.assistant_config.get("normalization", {})
        ) or self.get_known_branch(variables)

        if branch:
            arguments["branch"] = branch

        arguments["conversation_id"] = deep_get(variables, "conversation_id", "")
        arguments["user_id"] = deep_get(variables, "user_id", "")

        tool_result = context.tool_runner.call(
            tool_name=tool_name,
            operation=operation,
            arguments=compact_dict(arguments)
        )

        observations.append({
            "subagent": self.name,
            "operation": operation,
            "arguments": arguments,
            "result": tool_result
        })

        tool_calls_used += 1

        updated_variables = apply_tool_update_rules(
            assistant_config=context.assistant_config,
            variables=variables,
            operation=operation,
            arguments=arguments,
            result=tool_result
        )

        repair_updates: Dict[str, Any] = {}

        if branch:
            repair_updates["selected_branch"] = branch
            repair_updates["location_branch"] = branch

        if appointment_date:
            repair_updates[config.get("appointment_date_path", "appointment_date")] = appointment_date
            repair_updates["date"] = appointment_date

        if date_text:
            repair_updates[config.get("date_text_path", "date_text")] = date_text

        if repair_updates:
            updated_variables = apply_variable_patch(updated_variables, repair_updates, [])

        template_key = "booking_confirmed" if tool_result.get("ok") is True else "booking_failed"

        answer = render_template(
            config.get("templates", {}).get(template_key, template_key.upper()),
            {
                "variables": updated_variables,
                "result": tool_result,
                "arguments": arguments,
                "message": context.user_message
            }
        )

        return SubagentResult(
            handled=True,
            action="reply",
            answer=answer,
            variable_updates=updated_variables,
            observations=observations,
            selected_subagent=self.name,
            tool_calls_used=tool_calls_used,
            notes="create booking attempted"
        )

    def resolve_slot_selection(
        self,
        context: SubagentContext,
        config: Dict[str, Any],
        variables: Dict[str, Any]
    ) -> Optional[Dict[str, Any]]:
        slots_path = config.get("available_slots_path", "available_slots")
        slots = deep_get(variables, slots_path, [])

        if not isinstance(slots, list) or not slots:
            return None

        resolver = config.get("slot_resolver", {})
        normalization = context.assistant_config.get("normalization", {})
        normalized_message = normalize_text(context.user_message, normalization)

        ordinal_map = resolver.get("ordinal_map", {})

        if isinstance(ordinal_map, dict):
            for phrase, index in ordinal_map.items():
                normalized_phrase = normalize_text(str(phrase), normalization)

                if normalized_phrase and normalized_phrase in normalized_message:
                    try:
                        i = int(index)
                    except Exception:
                        continue

                    if 0 <= i < len(slots) and isinstance(slots[i], dict):
                        return slots[i]

        time_field = resolver.get("time_field")
        time_normalizer = resolver.get("time_normalizer")

        if time_field and time_normalizer == "hour_exact_or_same_hour":
            requested_time = self.extract_time(context.user_message, config, normalization)

            if requested_time:
                for slot in slots:
                    slot_time = self.extract_time(str(deep_get(slot, time_field, "")), config, normalization)

                    if slot_time == requested_time:
                        return slot

                requested_hour = requested_time.split(":")[0]

                for slot in slots:
                    slot_time = self.extract_time(str(deep_get(slot, time_field, "")), config, normalization)

                    if slot_time and slot_time.split(":")[0] == requested_hour:
                        return slot

        return None

    def extract_time(self, text: str, config: Dict[str, Any], normalization: Dict[str, Any]) -> str:
        time_config = config.get("time_normalization", {})
        digit_map = normalization.get("digit_map", {})

        raw_text = str(text or "")

        for src, dst in time_config.get("replacements", {}).items():
            raw_text = raw_text.replace(src, dst)

        for src, dst in digit_map.items():
            raw_text = raw_text.replace(src, dst)

        normalized = normalize_text(raw_text, normalization)
        normalized = self.replace_arabic_time_words(normalized)

        regex = time_config.get("regex", r"(\d{1,2})(?::(\d{2}))?\s*(am|pm)?")

        try:
            match = re.search(regex, normalized, flags=re.IGNORECASE)
        except re.error:
            return ""

        if not match:
            return ""

        hour = int(match.group(1))
        minute = int(match.group(2) or "0")
        suffix = (match.group(3) or "").lower()

        if suffix == "pm" and hour < 12:
            hour += 12

        if suffix == "am" and hour == 12:
            hour = 0

        if hour < 0 or hour > 23 or minute < 0 or minute > 59:
            return ""

        return f"{hour:02d}:{minute:02d}"

