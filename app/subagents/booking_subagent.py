    def resolve_missing_template_key(
        self,
        config: Dict[str, Any],
        missing: List[str]
    ) -> str:
        normalized_missing = sorted([str(item or "").strip() for item in missing if str(item or "").strip()])
        joined_missing = "|".join(normalized_missing)

        by_paths = config.get("missing_template_by_paths", {})
        if isinstance(by_paths, dict):
            for key, template_key in by_paths.items():
                configured = sorted([
                    part.strip()
                    for part in str(key or "").split("|")
                    if part.strip()
                ])

                if configured == normalized_missing:
                    return str(template_key or "missing_fields").strip() or "missing_fields"

        rules = config.get("missing_template_rules", [])
        if isinstance(rules, list):
            for rule in rules:
                if not isinstance(rule, dict):
                    continue

                paths = rule.get("paths", [])
                if not isinstance(paths, list):
                    continue

                configured = sorted([str(item or "").strip() for item in paths if str(item or "").strip()])
                if configured == normalized_missing:
                    return str(rule.get("template_key") or rule.get("template") or "missing_fields").strip() or "missing_fields"

        return str(config.get("default_missing_template_key", "missing_fields") or "missing_fields")

    def extract_customer_details(
        self,
        message: str,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        extracted = extract_by_patterns(
            message=message,
            patterns=config.get("extraction_patterns", []),
            variables=variables,
            normalization_config=normalization
        )

        if not isinstance(extracted, dict):
            extracted = {}

        if self.is_control_only_message(message, config, normalization):
            extracted.pop("customer_profile.full_name", None)

        fallback = self.extract_customer_details_fallback(
            message=message,
            config=config,
            variables=variables,
            normalization=normalization
        )

        for key, value in fallback.items():
            if key not in extracted or extracted.get(key) in [None, "", [], {}]:
                extracted[key] = value

        if "customer_profile.full_name" in extracted:
            full_name = str(extracted.get("customer_profile.full_name") or "").strip()

            if (
                self.is_control_only_message(message, config, normalization)
                or not self.message_has_name_signal(message, config, normalization)
                or not self.looks_like_person_name(full_name, config, normalization)
            ):
                extracted.pop("customer_profile.full_name", None)

        if not extracted:
            return {}

        return self.post_process_extracted_fields(extracted, config, normalization)


    def extract_customer_details_fallback(
        self,
        message: str,
        config: Dict[str, Any],
        variables: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        output: Dict[str, Any] = {}
        text = str(message or "").strip()

        phone_config = config.get("phone_extraction", {})
        phone_regex = phone_config.get("regex", r"(01\d{9}|\+?20\d{10,11})")

        if self.is_same_phone_request(text, config, normalization):
            existing_phone = self.get_existing_phone(variables, config, normalization)
            if existing_phone:
                output["customer_profile.phone"] = existing_phone

        if not deep_get(variables, "customer_profile.phone") and not output.get("customer_profile.phone"):
            try:
                phone_match = re.search(
                    phone_regex,
                    normalize_digits(text, normalization.get("digit_map", {}))
                )
            except re.error:
                phone_match = None

            if phone_match:
                output["customer_profile.phone"] = phone_match.group(1).strip()

        if (
            not deep_get(variables, "customer_profile.full_name")
            and self.message_has_name_signal(text, config, normalization)
            and not self.is_control_only_message(text, config, normalization)
        ):
            name = self.extract_name_from_configured_patterns(
                message=text,
                config=config
            )

            if name and self.looks_like_person_name(name, config, normalization):
                output["customer_profile.full_name"] = name

        if not deep_get(variables, "customer_profile.plate_number"):
            plate = self.extract_plate_fallback(text, config, normalization)

            if plate:
                output["customer_profile.plate_number"] = plate

        return output


    def ensure_customer_profile_aliases(
        self,
        variables: Dict[str, Any],
        persistence_config: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Repair profile aliases before required-field checks.

        Alias rules are config-driven through:
        customer_profile_persistence.aliases = [
          {"source_path": "customer_profile.name", "target_path": "customer_profile.full_name", "validator": "full_name"},
          ...
        ]

        A small generic compatibility rule also maps sibling "name" to
        "full_name" for customer_profile-like objects when no explicit alias
        list is configured.
        """
        alias_rules = persistence_config.get("aliases", [])
        if not isinstance(alias_rules, list):
            alias_rules = []

        if not alias_rules:
            alias_rules = [
                {
                    "source_path": "customer_profile.name",
                    "target_path": "customer_profile.full_name",
                    "backup_paths": ["booking.customer_profile.full_name"],
                    "validator": "full_name"
                },
                {
                    "source_path": "booking.customer_profile.name",
                    "target_path": "booking.customer_profile.full_name",
                    "backup_paths": ["customer_profile.full_name"],
                    "validator": "full_name"
                }
            ]

        updates: Dict[str, Any] = {}

        for rule in alias_rules:
            if not isinstance(rule, dict):
                continue

            source_path = str(rule.get("source_path") or "").strip()
            target_path = str(rule.get("target_path") or "").strip()
            validator = str(rule.get("validator") or "").strip()

            if not source_path or not target_path:
                continue

            target_value = deep_get(variables, target_path)
            if target_value not in [None, "", [], {}]:
                continue

            value = self.normalize_customer_profile_value(
                deep_get(variables, source_path),
                validator,
                config,
                normalization
            )

            if not value:
                continue

            updates[target_path] = value

            backup_paths = rule.get("backup_paths", [])
            if isinstance(backup_paths, list):
                for backup_path in backup_paths:
                    backup_path_text = str(backup_path or "").strip()
                    if backup_path_text and deep_get(variables, backup_path_text) in [None, "", [], {}]:
                        updates[backup_path_text] = value

        if not updates:
            return variables

        return apply_variable_patch(variables, updates, [])



    def ensure_customer_profile_context(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        message: str = ""
    ) -> Dict[str, Any]:
        """
        Preserve and merge customer_profile fields across booking turns.

        This is intentionally generic and config-driven:
        - field names are read from customer_profile_persistence.fields
        - source/backup paths are read from customer_profile_persistence.source_paths
        - backup targets are read from customer_profile_persistence.backup_paths

        It prevents a later partial extraction, such as only plate_number,
        from causing a previously captured full_name or phone to disappear.
        """
        persistence_config = config.get("customer_profile_persistence", {})
        if not isinstance(persistence_config, dict):
            persistence_config = {}

        fields_config = persistence_config.get("fields", {})
        if not isinstance(fields_config, dict):
            fields_config = {}

        variables = self.ensure_customer_profile_aliases(
            variables=variables,
            persistence_config=persistence_config,
            config=config,
            normalization=normalization
        )

        default_fields = {
            "full_name": {
                "target_path": "customer_profile.full_name",
                "source_paths": [
                    "customer_profile.full_name",
                    "customer_profile.name",
                    "booking.customer_profile.full_name",
                    "booking.customer_profile.name",
                    "booking.pending.customer_profile.full_name",
                    "booking.pending.customer_profile.name",
                    "booking.confirmed.full_name",
                    "booking.confirmed.name"
                ],
                "backup_paths": [
                    "booking.customer_profile.full_name"
                ],
                "validator": "full_name"
            },
            "phone": {
                "target_path": "customer_profile.phone",
                "source_paths": [
                    "customer_profile.phone",
                    "booking.customer_profile.phone",
                    "booking.pending.customer_profile.phone",
                    "booking.confirmed.phone",
                    "phone",
                    "customer_phone",
                    "user_id"
                ],
                "backup_paths": [
                    "booking.customer_profile.phone"
                ],
                "validator": "phone"
            },
            "plate_number": {
                "target_path": "customer_profile.plate_number",
                "source_paths": [
                    "customer_profile.plate_number",
                    "booking.customer_profile.plate_number",
                    "booking.pending.customer_profile.plate_number",
                    "booking.confirmed.plate_number"
                ],
                "backup_paths": [
                    "booking.customer_profile.plate_number"
                ],
                "validator": "plate_number"
            }
        }

        updates: Dict[str, Any] = {}

        for field_name, default_field_config in default_fields.items():
            field_config = fields_config.get(field_name, {})
            if not isinstance(field_config, dict):
                field_config = {}

            target_path = str(
                field_config.get("target_path")
                or default_field_config.get("target_path")
                or ""
            ).strip()

            if not target_path:
                continue

            source_paths = field_config.get("source_paths")
            if not isinstance(source_paths, list) or not source_paths:
                source_paths = default_field_config.get("source_paths", [])

            backup_paths = field_config.get("backup_paths")
            if not isinstance(backup_paths, list):
                backup_paths = default_field_config.get("backup_paths", [])

            validator = str(
                field_config.get("validator")
                or default_field_config.get("validator")
                or ""
            ).strip()

            value = self.first_valid_customer_profile_value(
                variables=variables,
                source_paths=source_paths,
                validator=validator,
                config=config,
                normalization=normalization
            )

            if value in [None, ""]:
                continue

            current_value = deep_get(variables, target_path)

            if current_value in [None, ""]:
                updates[target_path] = value
            else:
                normalized_current = self.normalize_customer_profile_value(
                    current_value,
                    validator,
                    config,
                    normalization
                )

                if normalized_current and normalized_current != current_value:
                    updates[target_path] = normalized_current

            for backup_path in backup_paths:
                backup_path_text = str(backup_path or "").strip()
                if not backup_path_text:
                    continue

                backup_value = deep_get(variables, backup_path_text)
                if backup_value in [None, ""]:
                    updates[backup_path_text] = value

        if not updates:
            return variables

        return apply_variable_patch(variables, updates, [])

    def first_valid_customer_profile_value(
        self,
        variables: Dict[str, Any],
        source_paths: List[str],
        validator: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        for source_path in source_paths:
            path_text = str(source_path or "").strip()

            if not path_text:
                continue

            value = self.normalize_customer_profile_value(
                deep_get(variables, path_text),
                validator,
                config,
                normalization
            )

            if value:
                return value

        return ""

    def normalize_customer_profile_value(
        self,
        value: Any,
        validator: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        text = str(value or "").strip()

        if not text:
            return ""

        if validator == "phone":
            return self.normalize_phone_value(text, config, normalization)

        if validator == "full_name":
            cleaned = self.clean_name(text, config)
            if self.looks_like_person_name(cleaned, config, normalization):
                return cleaned
            return ""

        if validator == "plate_number":
            plate = self.normalize_plate_number(text, normalization)
            if self.looks_like_plate_number(plate, config, normalization):
                return plate
            return ""

        return text

    def ensure_phone_context(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        message: str = ""
    ) -> Dict[str, Any]:
        """
        Promote an already-known phone value into customer_profile.phone.

        The source paths and same-phone markers are config-driven. This keeps
        WhatsApp/user-id phone context usable for create_booking without asking
        the customer for phone again.
        """
        existing_profile_phone = self.normalize_phone_value(
            deep_get(variables, "customer_profile.phone"),
            config,
            normalization
        )

        if existing_profile_phone:
            if existing_profile_phone == deep_get(variables, "customer_profile.phone"):
                return variables

            return apply_variable_patch(
                variables,
                {"customer_profile.phone": existing_profile_phone},
                []
            )

        existing_phone = self.get_existing_phone(variables, config, normalization)

        if not existing_phone:
            return variables

        updates = {"customer_profile.phone": existing_phone}

        phone_reuse = config.get("phone_reuse", {})
        if isinstance(phone_reuse, dict) and self.is_same_phone_request(message, config, normalization):
            confirmed_path = str(phone_reuse.get("confirmed_path", "") or "").strip()
            if confirmed_path:
                updates[confirmed_path] = True

        return apply_variable_patch(variables, updates, [])

    def get_existing_phone(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        phone_reuse = config.get("phone_reuse", {})
        phone_config = config.get("phone_extraction", {})

        if not isinstance(phone_reuse, dict):
            phone_reuse = {}

        if not isinstance(phone_config, dict):
            phone_config = {}

        source_paths: List[str] = []

        for source in [
            phone_reuse.get("source_paths", []),
            phone_config.get("source_paths", [])
        ]:
            if isinstance(source, list):
                source_paths.extend([str(item or "") for item in source if str(item or "").strip()])

        if not source_paths:
            source_paths = [
                "customer_profile.phone",
                "phone",
                "customer_phone",
                "user_id"
            ]

        for source_path in source_paths:
            value = self.normalize_phone_value(
                deep_get(variables, source_path),
                config,
                normalization
            )

            if value:
                return value

        return ""

    def normalize_phone_value(
        self,
        value: Any,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        raw_value = str(value or "").strip()

        if not raw_value:
            return ""

        phone_config = config.get("phone_extraction", {})
        if not isinstance(phone_config, dict):
            phone_config = {}

        regex = str(phone_config.get("regex", r"(01\d{9}|\+?20\d{10,11})") or "")
        digits_text = normalize_digits(raw_value, normalization.get("digit_map", {}))

        if regex:
            try:
                match = re.search(regex, digits_text)
            except re.error:
                match = None

            if match:
                return match.group(1).strip()

        digits_only = re.sub(r"\D+", "", digits_text)
        min_digits = int(phone_config.get("min_digits", 10) or 10)
        max_digits = int(phone_config.get("max_digits", 15) or 15)

        if min_digits <= len(digits_only) <= max_digits:
            return digits_only

        return ""

