    def extract_date_text(self, message: str, config: Dict[str, Any], normalization: Dict[str, Any]) -> str:
        raw_message = normalize_digits(str(message or ""), normalization.get("digit_map", {})).strip()
        normalized = normalize_text(raw_message, normalization)

        # Do not let time-only slot selections become date_text.
        # Example during slot_selection: a time-only reply should resolve
        # to a slot time, not to date_text.
        if self.looks_like_time_only_slot_selection_text(
            message=raw_message,
            config=config,
            normalization=normalization
        ):
            return ""

        direct = self.extract_direct_date_term(
            normalized_message=normalized,
            config=config,
            normalization=normalization
        )

        if direct:
            return direct

        explicit = self.extract_explicit_date_phrase(raw_message, config)

        if explicit:
            return explicit

        for item in config.get("date_extraction_patterns", []):
            if not isinstance(item, dict):
                continue

            regex = item.get("regex", "")
            group = int(item.get("group", 1))

            if not regex:
                continue

            try:
                match = re.search(regex, raw_message, flags=re.IGNORECASE)
            except re.error:
                continue

            if match:
                value = self.clean_date_text(match.group(group).strip(), normalization, config)
                if value:
                    return value

        return ""


    def looks_like_time_only_slot_selection_text(
        self,
        message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> bool:
        raw_message = str(message or "").strip()

        if not raw_message:
            return False

        normalized = normalize_text(raw_message, normalization)

        date_terms = config.get("date_direct_terms", [])

        if isinstance(date_terms, list):
            ambiguous_booking_words = set()
            time_only_config = config.get("time_only_slot_selection", {})
            if isinstance(time_only_config, dict):
                raw_ambiguous = time_only_config.get("ambiguous_date_terms", [])
                if isinstance(raw_ambiguous, list):
                    ambiguous_booking_words = {
                        normalize_text(str(item or ""), normalization)
                        for item in raw_ambiguous
                        if str(item or "").strip()
                    }

            for term in date_terms:
                term_text = str(term or "").strip()

                if not term_text:
                    continue

                normalized_term = normalize_text(term_text, normalization)

                if normalized_term in ambiguous_booking_words:
                    continue

                if normalized_term and normalized_term in normalized:
                    return False

        if self.extract_explicit_date_phrase(raw_message, config):
            return False

        requested_time = self.extract_time(raw_message, config, normalization)

        if not requested_time:
            return False

        time_only_config = config.get("time_only_slot_selection", {})
        if not isinstance(time_only_config, dict):
            time_only_config = {}

        markers = time_only_config.get("time_markers", [])

        if not isinstance(markers, list):
            markers = []

        for marker in markers:
            normalized_marker = normalize_text(str(marker or ""), normalization)
            if normalized_marker and normalized_marker in normalized:
                return True

        allow_bare_numeric = bool(time_only_config.get("allow_bare_numeric", True))

        if not allow_bare_numeric:
            return False

        bare_numeric_regex = time_only_config.get(
            "bare_numeric_regex",
            r"(^|\D)(\d{1,2})(?::\d{2})?(\D|$)"
        )

        try:
            return bool(re.search(str(bare_numeric_regex), normalized))
        except re.error:
            return False

    def extract_direct_date_term(
        self,
        normalized_message: str,
        config: Dict[str, Any],
        normalization: Dict[str, Any]
    ) -> str:
        direct_terms = config.get("date_direct_terms", [])

        for term in direct_terms:
            normalized_term = normalize_text(str(term), normalization)

            if normalized_term and normalized_term in normalized_message:
                return str(term)

        return ""


    def extract_explicit_date_phrase(self, raw_message: str, config: Optional[Dict[str, Any]] = None) -> str:
        config = config or {}
        patterns = config.get("explicit_date_patterns", [])

        if not isinstance(patterns, list):
            patterns = []

        for item in patterns:
            pattern = ""
            group = 1

            if isinstance(item, dict):
                pattern = str(item.get("regex", "") or "")
                try:
                    group = int(item.get("group", 1))
                except Exception:
                    group = 1
            else:
                pattern = str(item or "")

            if not pattern:
                continue

            try:
                match = re.search(pattern, raw_message, flags=re.IGNORECASE)
            except re.error:
                continue

            if match:
                try:
                    return match.group(group).strip()
                except Exception:
                    return match.group(0).strip()

        return ""


    def clean_date_text(
        self,
        value: str,
        normalization: Dict[str, Any],
        config: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}
        text = str(value or "").strip()

        if not text:
            return ""

        normalized = normalize_text(text, normalization)

        direct_terms = config.get("date_direct_terms", [])

        if not isinstance(direct_terms, list):
            direct_terms = []

        for term in direct_terms:
            normalized_term = normalize_text(str(term or ""), normalization)
            if normalized_term and normalized_term in normalized:
                return str(term)

        explicit = self.extract_explicit_date_phrase(text, config)

        if explicit:
            return explicit

        cleanup_config = config.get("date_text_cleanup", {})
        if not isinstance(cleanup_config, dict):
            cleanup_config = {}

        strip_prefix_patterns = cleanup_config.get("strip_prefix_patterns", [])
        cut_patterns = cleanup_config.get("cut_patterns", [])

        if isinstance(strip_prefix_patterns, list):
            for pattern in strip_prefix_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue

                try:
                    text = re.sub(pattern_text, "", text).strip()
                except re.error:
                    continue

        if isinstance(cut_patterns, list):
            for pattern in cut_patterns:
                pattern_text = str(pattern or "").strip()
                if not pattern_text:
                    continue

                try:
                    text = re.split(pattern_text, text, maxsplit=1)[0].strip()
                except re.error:
                    continue

        return text

    def ensure_resolved_date_from_existing_state(
        self,
        variables: Dict[str, Any],
        config: Dict[str, Any],
        normalization: Dict[str, Any],
        message: str = ""
    ) -> Dict[str, Any]:
        appointment_date_path = config.get("appointment_date_path", "appointment_date")
        date_text_path = config.get("date_text_path", "date_text")

        existing_date = deep_get(variables, appointment_date_path) or deep_get(variables, "date")

        if existing_date:
            return variables

        date_text = deep_get(variables, date_text_path, "")

        if not date_text and message:
            date_text = self.extract_date_text(message, config, normalization)

        if not date_text:
            return variables

        resolved_date = self.resolve_date_text(
            date_text=str(date_text),
            message=message,
            normalization=normalization,
            config=config
        )

        if not resolved_date:
            return variables

        return apply_variable_patch(
            variables,
            {
                date_text_path: date_text,
                appointment_date_path: resolved_date,
                "date": resolved_date
            },
            []
        )

    def resolve_date_text(
        self,
        date_text: str,
        message: str = "",
        normalization: Optional[Dict[str, Any]] = None,
        config: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}
        normalization = normalization or {}
        raw = normalize_digits(str(date_text or "").strip(), normalization.get("digit_map", {}))
        raw_message = normalize_digits(str(message or "").strip(), normalization.get("digit_map", {}))

        if not raw:
            return ""

        normalized = normalize_text(raw, normalization)
        normalized_message = normalize_text(raw_message, normalization) if raw_message else normalized

        today = datetime.now(TZ).date()

        relative_days = self.resolve_relative_day_offset(
            normalized=normalized,
            config=config,
            normalization=normalization
        )

        if relative_days is not None:
            return (today + timedelta(days=relative_days)).isoformat()

        weekday_index = self.resolve_weekday_index(
            normalized=normalized,
            config=config,
            normalization=normalization
        )

        if weekday_index is None and raw_message:
            weekday_index = self.resolve_weekday_index(
                normalized=normalized_message,
                config=config,
                normalization=normalization
            )

        if weekday_index is not None:
            force_next_week = (
                self.has_next_week_marker(normalized, config, normalization)
                or self.has_next_week_marker(normalized_message, config, normalization)
            )
            return self.next_weekday(today, weekday_index, force_next_week=force_next_week).isoformat()

        iso = self.parse_iso_date(raw)

        if iso:
            return iso

        numeric = self.parse_numeric_date(raw, today)

        if numeric:
            return numeric

        month_named = self.parse_named_month_date(raw, today, config=config, normalization=normalization)

        if month_named:
            return month_named

        return ""

    def resolve_relative_day_offset(
        self,
        normalized: str,
        config: Optional[Dict[str, Any]] = None,
        normalization: Optional[Dict[str, Any]] = None
    ) -> Optional[int]:
        config = config or {}
        normalization = normalization or {}
        date_resolution = config.get("date_resolution", {})
        if not isinstance(date_resolution, dict):
            date_resolution = {}

        relative_terms = date_resolution.get("relative_day_offsets", {})
        if not isinstance(relative_terms, dict):
            relative_terms = {}

        for key, value in relative_terms.items():
            # Supported shapes:
            # 1) {"1": ["tomorrow", "..."]}
            # 2) {"tomorrow": 1, "...": 1}
            offset: Optional[int] = None
            terms: List[Any] = []

            try:
                offset = int(key)
                if isinstance(value, list):
                    terms = value
                else:
                    terms = [value]
            except Exception:
                try:
                    offset = int(value)
                    terms = [key]
                except Exception:
                    offset = None
                    terms = []

            if offset is None:
                continue

            for term in terms:
                normalized_term = normalize_text(str(term or ""), normalization)
                if normalized_term and normalized_term in normalized:
                    return offset

        return None

    def resolve_weekday_index(
        self,
        normalized: str,
        config: Optional[Dict[str, Any]] = None,
        normalization: Optional[Dict[str, Any]] = None
    ) -> Optional[int]:
        config = config or {}
        normalization = normalization or {}
        date_resolution = config.get("date_resolution", {})
        if not isinstance(date_resolution, dict):
            date_resolution = {}

        weekday_terms = date_resolution.get("weekday_terms", {})
        if not isinstance(weekday_terms, dict):
            return None

        for key, value in weekday_terms.items():
            # Supported shapes:
            # 1) {"6": ["sunday", "..."]}
            # 2) {"sunday": 6, "...": 6}
            index: Optional[int] = None
            terms: List[Any] = []

            try:
                index = int(key)
                if isinstance(value, list):
                    terms = value
                else:
                    terms = [value]
            except Exception:
                try:
                    index = int(value)
                    terms = [key]
                except Exception:
                    index = None
                    terms = []

            if index is None:
                continue

            for term in terms:
                normalized_term = normalize_text(str(term or ""), normalization)
                if normalized_term and normalized_term in normalized:
                    return index

        return None

    @staticmethod
    def has_next_week_marker(
        normalized: str,
        config: Optional[Dict[str, Any]] = None,
        normalization: Optional[Dict[str, Any]] = None
    ) -> bool:
        config = config or {}
        normalization = normalization or {}
        date_resolution = config.get("date_resolution", {})
        if not isinstance(date_resolution, dict):
            date_resolution = {}

        markers = date_resolution.get("next_week_markers", [])
        if not isinstance(markers, list):
            return False

        for marker in markers:
            normalized_marker = normalize_text(str(marker or ""), normalization)
            if normalized_marker and normalized_marker in str(normalized or ""):
                return True

        return False

    def next_weekday(self, today, weekday_index: int, force_next_week: bool = False):
        # Instance method because resolve_date_text calls self.next_weekday(...).
        # Keep this signature stable to avoid TypeError during weekday normalization.
        days_ahead = weekday_index - today.weekday()

        if days_ahead < 0:
            days_ahead += 7

        if force_next_week and days_ahead == 0:
            days_ahead = 7

        return today + timedelta(days=days_ahead)

    def parse_iso_date(self, raw: str) -> str:
        match = re.search(r"\b(\d{4})-(\d{1,2})-(\d{1,2})\b", raw)

        if not match:
            return ""

        year = int(match.group(1))
        month = int(match.group(2))
        day = int(match.group(3))

        try:
            return datetime(year, month, day).date().isoformat()
        except ValueError:
            return ""

    def parse_numeric_date(self, raw: str, today) -> str:
        match = re.search(r"\b(\d{1,2})[/-](\d{1,2})(?:[/-](\d{2,4}))?\b", raw)

        if not match:
            return ""

        day = int(match.group(1))
        month = int(match.group(2))
        year_text = match.group(3)

        if year_text:
            year = int(year_text)
            if year < 100:
                year += 2000
        else:
            year = today.year

        try:
            candidate = datetime(year, month, day).date()
        except ValueError:
            return ""

        if not year_text and candidate < today:
            try:
                candidate = datetime(year + 1, month, day).date()
            except ValueError:
                return ""

        return candidate.isoformat()

    def parse_named_month_date(
        self,
        raw: str,
        today,
        config: Optional[Dict[str, Any]] = None,
        normalization: Optional[Dict[str, Any]] = None
    ) -> str:
        config = config or {}
        normalization = normalization or {}
        date_resolution = config.get("date_resolution", {})
        if not isinstance(date_resolution, dict):
            date_resolution = {}

        month_config = (
            date_resolution.get("month_terms")
            if isinstance(date_resolution.get("month_terms"), dict)
            else date_resolution.get("month_names", {})
        )

        if not isinstance(month_config, dict):
            return ""

        normalized = normalize_text(raw, normalization)

        pattern = str(
            date_resolution.get("named_month_date_regex")
            or date_resolution.get("named_month_pattern")
            or r"\b(\d{1,2})\s+([^\s\d]+)(?:\s+(\d{4}))?\b"
        )

        try:
            match = re.search(pattern, normalized)
        except re.error:
            return ""

        if not match:
            return ""

        day = int(match.group(1))
        month_name = normalize_text(match.group(2), normalization)
        year_text = match.group(3)

        month = None

        for key, value in month_config.items():
            # Supported shapes:
            # 1) {"6": ["يونيو", "..."]}
            # 2) {"يونيو": 6, "...": 6}
            candidate_month = None
            terms: List[Any] = []

            try:
                candidate_month = int(key)
                terms = value if isinstance(value, list) else [value]
            except Exception:
                try:
                    candidate_month = int(value)
                    terms = [key]
                except Exception:
                    candidate_month = None
                    terms = []

            if candidate_month is None:
                continue

            for term in terms:
                normalized_term = normalize_text(str(term or ""), normalization)
                if normalized_term and normalized_term == month_name:
                    month = candidate_month
                    break

            if month is not None:
                break

        if not month:
            return ""

        year = int(year_text) if year_text else today.year

        try:
            candidate = datetime(year, month, day).date()
        except ValueError:
            return ""

        if not year_text and candidate < today:
            try:
                candidate = datetime(year + 1, month, day).date()
            except ValueError:
                return ""

        return candidate.isoformat()

    def get_configured_path_list(
        self,
        config: Dict[str, Any],
        key_path: str,
        default: Optional[List[str]] = None
    ) -> List[str]:
        current: Any = config

        for part in str(key_path or "").split("."):
            if not isinstance(current, dict):
                current = None
                break
            current = current.get(part)

        if isinstance(current, list):
            return [str(item) for item in current if str(item or "").strip()]

        return [str(item) for item in (default or []) if str(item or "").strip()]

