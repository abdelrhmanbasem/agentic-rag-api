    def condition_matches(
        self,
        condition: Any,
        variables: Dict[str, Any],
        context: Optional[Dict[str, Any]] = None
    ) -> bool:
        if not isinstance(condition, dict):
            return False

        context = context or {"variables": variables}

        if "all" in condition:
            return all(
                self.condition_matches(item, variables, context=context)
                for item in self.config_list(condition, "all")
                if isinstance(item, dict)
            )

        if "any" in condition:
            return any(
                self.condition_matches(item, variables, context=context)
                for item in self.config_list(condition, "any")
                if isinstance(item, dict)
            )

        if "not" in condition and isinstance(condition.get("not"), dict):
            return not self.condition_matches(condition.get("not"), variables, context=context)

        path = str(condition.get("path", "") or "").strip()

        if path:
            if path.startswith("context."):
                value = deep_get(context, path[len("context."):])
            else:
                value = deep_get(variables, path)
        else:
            value = None

        exists = value not in [None, "", [], {}]

        if condition.get("exists") is True and not exists:
            return False

        if condition.get("exists") is False and exists:
            return False

        if "equals" in condition and value != condition.get("equals"):
            return False

        if "not_equals" in condition and value == condition.get("not_equals"):
            return False

        if isinstance(condition.get("in"), list) and value not in condition.get("in"):
            return False

        if isinstance(condition.get("not_in"), list) and value in condition.get("not_in"):
            return False

        if condition.get("truthy") is True and not bool(value):
            return False

        if condition.get("falsy") is True and bool(value):
            return False

        if "contains" in condition and str(condition.get("contains")) not in str(value or ""):
            return False

        if "regex" in condition:
            try:
                if not re.search(str(condition.get("regex") or ""), str(value or ""), flags=re.IGNORECASE):
                    return False
            except re.error:
                return False

        return True

    def get_active_state_values(self, config: Dict[str, Any]) -> List[str]:
        configured = config.get("active_state_values", [])
        if isinstance(configured, list) and configured:
            return [str(item) for item in configured]

        active_state = str(config.get("active_state", "active") or "active")
        return [active_state]

    @staticmethod
    def config_path(config: Dict[str, Any], path: str, default: Any = None) -> Any:
        current: Any = config

        for part in str(path or "").split("."):
            if not part:
                continue

            if not isinstance(current, dict):
                return default

            if part not in current:
                return default

            current = current.get(part)

        return current if current is not None else default

    @staticmethod
    def config_list(config: Dict[str, Any], path: str) -> List[Any]:
        value = TroubleshootingSubagent.config_path(config, path, [])

        if isinstance(value, list):
            return value

        if value in [None, ""]:
            return []

        return [value]

    @staticmethod
    def config_dict(config: Dict[str, Any], path: str) -> Dict[str, Any]:
        value = TroubleshootingSubagent.config_path(config, path, {})

        return value if isinstance(value, dict) else {}

    @staticmethod
    def flatten_phrase_sources(sources: List[Any]) -> List[Any]:
        phrases: List[Any] = []

        for source in sources:
            if isinstance(source, list):
                phrases.extend(source)
            elif source not in [None, "", [], {}]:
                phrases.append(source)

        return phrases

    @staticmethod
    def safe_int(value: Any, default: int) -> int:
        try:
            return int(value)
        except Exception:
            return default

    @staticmethod
    def safe_float(value: Any, default: float) -> float:
        try:
            return float(value)
        except Exception:
            return default

    @staticmethod
    def get_config_bool(config: Dict[str, Any], path: str, default: bool = False) -> bool:
        value = TroubleshootingSubagent.config_path(config, path, default)

        if isinstance(value, bool):
            return value

        if value is None:
            return default

        return str(value).strip().lower() in ["1", "true", "yes", "y", "on"]
